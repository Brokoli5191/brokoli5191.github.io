<h4>Zusammenfassung</h4>

Die Arbeit beginnt mit einer kurzen Zusammenfassung der Schwartzschen
Distributionentheorie. Besonders wird dabei auf die Grenzen dieser 
Theorie eingegangen, vor allem auf die Probleme, die beim Versuch, eine
Multiplikation von Distributionen zu definieren, auftreten.<br>
Daran schlieszt sich eine breite Darstellung der Colombeauschen Theorie 
der verallgemeinerten Funktionen an, in deren Rahmen eine Multiplikation 
von Distributionen moeglich ist.<br>
In den folgenden Kapiteln werden Boost und ultrarelativistischer Limes 
(v gegen 1) von Coulombfeld und 
Reissner-Nordstrom-Loesung im Raum der Distributionen berechnet. 
Die dabei auftretenden Fragen im Zusammenhang mit 
der Multiplikation von Distributionen werden schlieszlich vom Standpunkt 
der Colombeau-Theorie aus diskutiert und interpretiert. <br>
