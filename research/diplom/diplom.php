<h4>Abstract</h4>

This work begins with a short summary of Schwartz's distribution theory. 
We take a close look at the limitations of this theory, especially at the 
problems that arise while trying to define a product of distributions. <br> 
Then follows a presentation of Colombeau's theory of New Generalized  
Functions, that provides the possibility of a multiplication of  
distributions. <br>
In the following chapters we compute the boost and ultrarelativistic limit 
(v to 1) of the Coulombfield and the Reissner-Nordstrom 
solution in the space of distributions. Finally we discuss the results 
involving products of distributions from the viewpoint of Colombeau's theory. 
<br>
