<p align="justify">
<b>General:</b> Lorentzian geometry is the differential geometry of Lorentzian manifolds, that is semi-Riemannian manifolds with an indefinite metric of index 1. Its particular importance comes from the fact that Lorentzian manifolds, also called spacetimes, act as the stage for General Relativity (GR), Albert Einstein's eminent theory of space, time and gravity. In fact GR's fundamental statment is contained in its field equations, also called Einstein equations, which state that the gravitational field is a property of spacetime and that its energy matter content is proportional to its curvature. In this sense GR is but the study of 4-dimensional Lorentzian manifolds which satisfy the Einstein equations.
<br><br>

The decisive difference between Riemannian and Lorentzian manifolds is that a Riemannian metric encodes the topological structure of the manifolds (as eg. seen from the Hopf-Rinow theorem) while a Lorentzian metric does not even induce a metric in the toplogical sense via its length functional. Instead it gives rise to the causal structure: the vectors in each tangent space fall into one of the distinct classes of timelike, null and spacelike vectors according to the sign of their norm.
<br><br>

<b>Aim and Contents:</b> We study Lorentzian manifolds, and, in particular, their local and global causal structure with the goal to cover the famous singularity theorems of Penrose and Hawking. These are truly milestones in the development of GR, in particular the 1965-paper by Roger Penrose which won him the 2020-Nobel Price in physics. (You can find the paper <a href="https://journals.aps.org/prl/abstract/10.1103/PhysRevLett.14.57" target="_blank">here</a>, and  <a href="https://arxiv.org/abs/1410.5226" target="_blank">here</a> you can find an appraisal written on the accassion of the centennial of GR in 2015). The theorems assert that under physically realistic conditions spacetimes generically become singular in the sense that they contain an incomplete causal geodesic. Moreover, they do not make any use of the field equations but only suppose a condition on the Ricci curvature along with some causality conditions, and so they are actually pure geometric results. If time permits we will cover as a further highlight a rather recent result by Bernal and Sanchez on the structure of globally hyperbolic spacetimes.<br><br> 

More specifically the topics of the course will be
<ul>
<li>Prerequisites: sectional curvature, semi-Riemannian submanifolds</li>
<li>Basic examples of spacetimes (Minkowski, (anti-)de Sitter, and Robertson-Walker spaces, Schwarzschild half-plane)  </li>
<li>Basic causality theory (local causality, causality conditions) </li>
<li>Calculus of variations (Jacobi fields, focal and conjugate points) </li>
<li>Global hyperbolicity (Cauchy hypersurfaces, developments, and horizons) </li>
<li>The singularity theorms of Penrose and Hawking </li>
<li>The stucture of globally hyperbolic spacetimes </li>
</ul>
<br><br>

