<b>General:</b> Riemannian geometry is the study of smooth manifolds which carry a Riemannian metric, i.e. a scalar product on each tangent space. This allows to define local notions of angle, length, volume and curvature and hence to transfer the bulk of the classical elementary differential geometry of surfaces into the setting of abstract manifolds. In particular, global properties of the manifold can be studied by integrating the local contributions.
<br><br>

Riemannian geometry has its birthplace in Bernhard Riemann's habilitation lecture <a href="http://www.maths.tcd.ie/pub/HistMath/People/Riemann/Geom/" target="_blank">Ueber die Hypothesen, welche der Geometrie zu Grunde liegen (On the Hypotheses on which Geometry is based)</a> of 1854. Especially since the second half of the 20th century it developped into one major branch of differential geometry with strong ties to group and representation theory as well as analysis and algebraic and differential topology. Finally it also lies at the mathematical foundations of Albert Einstein's General Relativity.
<br><br>

<b>Aim and Contents:</b> This is a first course on Riemannian geometry and provides a general introduction into the field. The natural major topics are
<ul>
 <li>(Semi-)Riemannian metrics and manifolds
 <li>The Levi-civita connection
 <li>Geodescis, the exponential map and convexity
 <li>Arclength and Riemannian distance
 <li>The Hopf Rinow theorem
 <li>Curvature
 <li>The Einstein equations 
</ul>

