            <h1>
                Literatur zur Vorlesung Distributionentheorie
                <br><font size="-1">M. Kunzinger, R. Steinbauer, Sommersemester 2002</font>
            </h1>
            <hr>

            <ol>
                <li>J. Barros-Neto, <em>An Introduction to the Theory of Distributions</em>
                    <br>(Marcel Dekker, New York, 1973).
                    <br>
                    Funktionalanalytisch, enth&#228;lt viele
                    wichtige Resultate (Stetigkeit diverser Faltungsoperationen, Anwendungen
                    auf lineare PDEs), st&#252;tzt sich in den Details stark auf das Buch von Horvath. 
                </li>

                <li>J. Dieudonn&#233;, <em>Grundz&#252;ge der modernen Analysis, Band III</em>
                    (Vieweg, Braunschweig, 1976).
                    <br>
                    Sehr pr&#228;ziser und abstrakter 
                    (funktionalanalytischer) Zugang, allerdings basierend auf B&#228;nden I und II,
                    daher ev. nicht unmittelbar lesbar.
                </li>

                <li>G. Friedlander, M. Joshi, <em>Introduction to the Theory of Distributions</em>
                    <br>(2nd Edition, Cambridge Universtiy Press, 1998).
                    <br>
                    Das Buch der (unserer) Wahl,
                    vermittelt die wesentlichen Techniken und Resultate auf direktem Weg. 
                    Stoffauswahl unter dem Gesichtspunkt der Verwendbarkeit in der 
                    Theorie der PDEs, dem mit Sicherheit wichtigsten Anwendungsgebiet.
                </li>

                <li>J. Horvath, <em>Topological Vector Spaces and Distributions</em>
                    <br>(Addison-Wesley, Reading, MA, 1966).
                    <br>
                    Eines der Standardwerke &#252;ber lokalkonvexe Vektorr&#228;ume. Da zuerst diese Theorie
                    entwickelt wird, muss man sich mit ca. 300 Seiten Funktionalanalysis
                    besch&#228;ftigen, bevor die erste Distribution behandelt wird...
                </li>

                <li>L. H&#246;rmander, <em>The Analysis of Linear Partial Differential Operators I</em>
                    <br>(2nd Edition, Springer, Berlin, 1990).
                    <br>
                    Der Gro&#223;meister schlechthin 
                    (Fieldsmedaille (1962) f&#252;r seine bahnbrechenden Arbeiten auf 
                    dem Gebiet der linearen PDEs und Distributionen). Schwer lesbar, aber 
                    worth the effort''.
                </li>

                <li>I. M. Gelfand, G. E. Schilow, <em>Verallgemeinerte Funktionen 
                    (Distributionen) I-III</em>
                    <br>(Deutscher Verlag der Wissenschaften, Berlin 1960-69).
                    <br>
                    Eines der Standardwerke. Der erste Band bietet viele konrete Rechnungen
                    und Beispiele. Im Band II werden topologische Vektorr&#228;ume und im Band III
                    PDEs behandelt. Es gibt auch einen Band IV von Gelfand und N. J. Wilenkin,
                    der Distributionskerne und harmonische Analyse beinhaltet.  
                </li>

                <li>L. Schwartz, <em>Th&#233;orie des Distributions</em>
                    (Hermann, Paris, 1966).
                    <br>
                    Das Buch des  Erfinders und  Grand Seigneurs der Theorie (Fields Medaille
                    (1950) f&#252;r die Entwicklung ebendieser Theorie!).
                </li>

                <li>F. Treves, <em>Topological Vector Spaces, Distributions and Kernels</em>
                    <br>(Pure and Applied Mathematics, Academic Press, New York, 1967).
                    <br>
                    Einf&#252;hrung in die Theorie der topologischen Vetorr&#228;ume mit starkem Bezug zu
                    Distributionentheorie und Anwendungen in PDEs.
                </li>

                <li>F. Treves, <em>Basic Linear Partial Differential Operators</em>
                    <br>(Pure and Applied Mathematics, Academic Press, New York, 1975).
                    <br>
                    Distributions in action.'' Ein Buch &#252;ber lineare PDEs mit einem 
                    Zugang bei dem Fundamentall&#246;sungen im Zentrum stehen. Die Struktur
                    von L&#246;sungen der klassischen Gleichungen werden &#252;ber das Studium 
                    ihrer Fundamentall&#246;sungen gewonnen.
                </li>

                <li>W. Walter, <em>Einf&#252;hrung in die Theorie der Distributionen</em>
                    (2. Auflage, BI, Z&#252;rich, 1974). 
                    <br>
                    Eine freundliche und detaillierte Einf&#252;hrung; leicht lesbar. 
                    In der Stoffauswahl &#228;hnlich wie aber etwas enger als das Buch von Friedlander.
                </li>
            </ol>
