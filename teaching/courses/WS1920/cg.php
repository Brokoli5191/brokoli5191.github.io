                <p align="justify">
                  <b>Contents and prerequsites:</b> This course provides an introduction to comparison geometry maninly of Riemannian manifolds with some excursions into the semi-Riemannian/Lorentzian
                  realm. We present the main results and discuss the main techniques of the theory assuming basic knowledge in Riemannian geometry (as provided by a typical standard course, see e.g.
                  our <a href="https://www.mat.univie.ac.at/~stein/teaching/SoSem16/rg-2017-03-06.pdf">lecture notes</a>) and of course basic konwledge of differential geometry (again as provided by a
                  typical first course, see e.g. the <a href="http://www.mat.univie.ac.at/~mike/teaching/ss08/dg.pdf"> lecture notes</a> of Mike Kunzinger). In addidtion we will use some basics on
                  covering spaces and also some ODE-theory. <br /><br />

                  We will, however, start the course by discussing some fundamentals of Riemannian geometry (to also allow students from different curricula as e.g. theoretical physics to enter the
                  course) such as geodesics, the exponential map, geodesic variations and Jacobi fields. As a first result we will prove <b>Myers' theorem</b> which states that a (complete, conected)
                  Riemannian manifold with Ricci-curvature bounded below by a positive constant is compact with an explicit bound on its diameter and a finite fundamental group. A further fundamental
                  result we will present is the <b>Rauch comparison theorem</b>, which relates the sectional curvature to the rate at which geodesics spread apart. Finally we will prove
                  <b>Toponogov's triangle comparison theorem</b> which is a precise version of the idea mentined above. <br /><br />

                  <b>Target audience</b> are primarily students in the mathematics master programme (specialising in geometry and topology) but also ambitious students in the theoretical physics
                  master programme. <br /><br />

                  <b>Position in the curriculum:</b> Master Mathematics, compulsary module "Electives in Geometry" code MGEV. This course is also part of the
