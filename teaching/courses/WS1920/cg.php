                      <p align="justify">
                        <b>General Remarks:</b> The basic idea of comparison geometry is to compare the geometry of an arbitrary Riemannian manifold M with the geometry of constant curvature spaces
                        (flat space, the spheres and hyperbolic spaces) to obtain information on the properties of M. To give a vivid description recall that triangles on a sphere are fatter than
                        triangles in flat space and that the sum of interior angles of a spherical triangle generally exceeds Pi. A typical statement in comparison geometry would now be that if M has
                        (sectional) curvature bounded below by some k>0 then the triangles of M are fatter than those of M_k, the sphere of curvature k. <br /><br />

                        The theory that is bulit upon such and similar ideas has lead to a wealth of important results that firmly belong to the standard tool-kit of Riemannian geometry. In
                        particular, this includes some of the most important theorems, which allow to obtain global information on the geometry and topology of Riemannian manifolds.
                      </p>
