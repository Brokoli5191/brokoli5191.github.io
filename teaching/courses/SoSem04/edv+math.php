              <h3>Beurteilung:</h3>
              <p>Es bestehen wahlweise zwei Möglichkeiten eine postive
              Beurteilung zu erreichen:</p>
              <ol>
                <li class="list-dot">Halten eines Seminarvortrags; hier sind sowohl anwendungsorientiertere
                Themen (etwa für Studierende des Lehramts Informatik und 
                Informatikmanagement), wie auch theoretischere möglich. Neben der 
                (selbstverständlichen) fachlichen Qualität des Vortrags ist mir das
                Vermitteln einer guten, dem Inhalt angepassten Präsentationstechnik
                ein besonderes Anliegen, das bei von mir angebotenen Vorbereitungstreffen 
                nicht zu kurz kommen wird.</li>
                <li class="list-dot">Ablegen einer klassischen (mündlichen) Prüfung über den Stoff der 
                Lehrveranstaltung.</li>
              </ol>
              
              <h3>Informationen zur Prüfung:</h3>
              <p>Teilnehmerinnen des Konversatoriums resp. des Seminars
              können eine mündliche Prüfung ablegen. Termine können mit mir 
              persönlich vereinbart werden (nicht zwischen 6.7. und 10.8. bzw.
              20.9. und 31.10.) Der Stoff für das Konversatoriums endet 
              mit Kapitel 12 (Postsches Korrespondezproblem; Vortrag Manfred Hubauer),
              der des Seminars beginnt mit Kapitel 13 (Nichthandhabbare Probleme; Vortrag
              Christoph Hödl). Das Kapitel 18 (Minesweeper ist NP-vollständig; 
              Abschlußvortrag von Christoph Marx) gehört nicht zum Prüfungsstoff.</p>
              
              <p>Die Prüfungskandidatinnen können sich eine beliebige Frage aus dem 
              Stoffgebiet selbst aussuchen; weitere Fragen kommen von mir. Die Dauer der
              Prüfung ist ca. 45 Minuten (nur Konversatorium) bzw. ca. 60 Minuten
              (Konversatorium und Seminar). Beweise müssen nur unter Verwendung 
              der Unterlagen erklärt werden können (der Rest natürlich ohne!). Die 
              vollständigen (und tw. korrigierten) Unterlagen können bei mir zum 
              Kopieren entlehnt werden.</p>
              
