              <h3>Allgemeines:</h3>
              <p>Die Präzisierung des Begriffs Algorithmus und eines umfassenden
              Begriffs formaler Sprachen stellen eine der herausragenden Leistungen 
              der Mathematik der letzten 100 Jahre dar. Das mathematische Studium
              dieser Begriffe hat nicht nur bahnbrechende geistesgeschichtliche
              Ergebnisse gebracht (Gödel Sätze), sondern auch die Entwicklung
              programmierbarer Rechenanlagen entscheidend beeinflusst und so die
              Grundlagen der Informatik gelegt.</p>
              
              <h3>Inhalt:</h3>
              <p>In dieser Lehrveranstaltung begeben wir uns auf einen mathematischen 
              Streifzug durch die Grundlagen der Informatik mit Hauptziel und 
              Schwerpunkt Komplexitätstheorie.</p>
              
              <p>Wir beginnen die LVA mit einem Abschnitt über <em>endliche Automaten</em> 
              und ihre Sprachen, sie sog. <em>regulären Sprachen</em>. Die Formalisierung des 
              Begriffs "Algorithmus" führt uns danach zum Modell der 
              <em>Turing-Maschine</em> und zur Frage nach der <em>Entscheidbarkeit</em> 
              von Problemen. Danach befassen wir uns mit der <em>Komplexität</em>
              von Problemen, insbesondere den Klassen der sog. N- und NP-vollständigen 
              Probleme und der Frage ob, <em>P=NP</em> gilt.</p>
              
              <p>Der Aufbau der Lehrveranstaltung orientiert sich stark am
              Standardlehrbuch von Hopcroft und Ullman (siehe Literatur), wobei die
              Schwerpunkte auf die Kapitel 2, 3, 7, 8, 12 und 13 (Nummerierung folgt der
              1979er-Auflage) gelegt werden.</p>
              
              <h3>Modus:</h3>
              <p>Das 2-std. Konversatorium besteht aus einem Vorlesungsteil 
              (am Anfang des Semesters) und einem seminarartigen 
              Teil in dem Studierende den Vortrag übernehmen.</p>
              
              <p>Das 1-std. Seminar kann nur gemeinsam mit dem Konversatorium 
              besucht werden (und dient dazu, das 2-std. Konversatorium als insgesamt 3-std. 
              Wahlpflichtveranstaltung für das Lehramtsstudium Informatik und 
              Informatikmanagement anrechenbar zu machen; Details siehe unten).
              Im Seminar übernehmen ebenfalls die Studierenden den Vortrag.</p>
              
              <p>Konversatorium und Seminar finden teilgeblock statt; das 
              Konversatorium bis Ende Mai, das Seminar im Juni. Die Details werden am
              2.3. in der LVA geklärt.</p>
              
              <h3>Voraussetzungen:</h3>
              <p>Sind lediglich die (Bereitschaft zum und 
              die) Freude am abstrakten Denken, sowie die Beherrschung einiger grundlegender 
              mathematischer Begriffe und Methoden wie etwa Mengen, Relationen, Funktionen
              und vollständige Induktion. Die Lehrveranstaltung ist also definitiv
              bereits für Studierende im ersten Abschnitt geeignet. Andererseits müssen
              Studierende im zweiten Abschnitt nicht befürchten unterfordert zu werden,
              da vieles am Stoff zwar elementar ist, aber knifflig!</p>
              
              <h3>Zielpublikum:</h3>
              <p>Prinzipiell alle am Thema Interessierten; das inkludiert insbesondere:</p>
              <ul>
                <li class="list-dot"><em>Diplomstudierende der Mathematik</em>, die 
                über Anwendungen ihres Fachs in (den Grundlagen) der Informatik 
                Bescheid wissen wollen.</li>
                <li class="list-dot"><em>Lehramtsstudierende der Mathematik</em>, die Interesse
                an den Zusammenhängen ihres Fachs mit den Grundlagen der Informatik 
                haben. Außerdem eignet sich der Themenbereich m.E. besonders gut
                für projektorientierten/fächerübergreifenden Unterricht—kombiniert etwa 
                mit Informatik und/oder Philosophie (Gödel Sätze) und/oder Geschichte 
                ("Ur-Geschichte" der Computer; es bestehen Querbeziehungen zur Kryptographie, i.e., Turing und seine 
                mechanischen "Rechenmaschinen" und ihre Rolle bei der 
                Entschlüsselung der Enigma).</li>
                <li class="list-dot">Studierende des <em>Lehramts Informatik und Informatikmanagements,</em> 
                die sich den Grundlagen ihres Faches von einer "mathematischen Seite" 
                her nähern wollen.</li>
              </ul>
              
