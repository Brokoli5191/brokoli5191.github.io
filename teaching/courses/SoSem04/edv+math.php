              <h2 class="h3 article-title">EDV und Mathematik</h2>
              <h3>Sommersemester 2004</h3>
              <h4>Roland Steinbauer</h4>
              
              <p><strong>Lehrveranstaltungsnummer:</strong> 877841, 877983</p>
              <p><strong>Lehrveranstaltungstyp:</strong> KO, SE</p>
              <p><strong>Stundenzahl:</strong> 2, 1</p>
