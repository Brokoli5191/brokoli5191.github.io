<b>Allgemeines:</b> Diese Vorlesung ist als Fortsetzung der <a href="../WS0708/fa.html">Funktionalanaylsis 1</a> vom
Wintersemester 2007/08 konzipiert und st&uuml;tzt sich nat&uuml;rlich wesentlich auf deren Inhalte. 

<br><br>

<b>Inhalt:</b> Die Vorlesung bietet weiterhin eine ausgewogene Einf&uuml;hrung in die Funktionalanalysis. Sie soll den
Studierenden (vor allem des Studienschwerpunkts "Analysis")  eine weitergehende Spezialisierung in den verschiedenen 
an der Fakult&auml;t vertretenen Arbeitsrichtungen erm&ouml;glichen. Etwas detaillierter gliedert sich der Inhalt wie folgt:
<ul>
<li>Operatoren auf Banach- und Hilbertr&auml;umen (kompakte und adjungierte Operatoren)
<li>Spektraltheorie (kompakter bzw. beschr&auml;nkter, selbstadjungierter Operatoren)
<li>Grundlagen der Theorie der lokalkonvexen Vektorr&auml;ume
</ul>
<br>

<b>Literatur:</b> In vielen Teilen der Vorlesung habe ich mich vom Buch von Dirk Werner (Funktionalanalysis, Springer,
 Berlin, 5. Auflage 2005) leiten lassen. Dar&uuml;berhinaus ist die <a href="../WS0708/fa_lit.pdf">kommentierte Literaturliste vom Wintersemester</a>
nat&uuml;rlich weiterhin aktuell. 
<br><br>

<b>Vorwissen:</b> Grundlegend zum Verst&auml;ndnis ist nat&uuml;rlich Teil 1 aus
dem Wintersemester bzw. eine gleichwertige Grundausbildung in 
(linearer, normierter) Funktionalanalysis. Ebenso erforderlich sind solide 
Kenntnisse aus Analysis, Topologie und Linearer Algebra 
(insbesondere Eigenwerttheorie).
In der Vorlesung wird explizit auf die entsprecheden Vorlesungen von 
G&uuml;nther H&ouml;rmann, Michael Grosser und Johannes Schoissengeier bezug genommen. 
In Fragen der Anwendungen werden Sie sich sicher leichter zurechtfinden, falls
Sie bereits die "partiellen Differentialgleichungen 1" besucht haben.
<br><br>

<B>Zielpublikum:</B> Diplomstudierende der Mathematik, aber auch 
Studierende der (theoretischen) Physik, die einen
Blick hinter die Kulissen riskieren wollen.
<br><br>

<b>Bemerkung:</b> Die Vorlesung findet teilgeblockt statt. Gew&ouml;hnlich
finden pro Woche 3 Einheiten zu 60 Minuten, also das &Auml;quivalent von 4 Semesterwochenstunden statt. Im Gegenzug 
entf&auml;llt die Vorlesung fallweise nach (entsprechend rechtzeitiger) Ank&uuml;ndigung.
<br><br>

<B>Positionierung im Studienplan:</B> Wahlpflichtfach im Studienschwerpunkt 
"Analysis" des Diplomstudiums Mathematik bzw. Bestandteil des Moduls 
"Standardausbildung im Studienschwerpunkt Analysis" im Masterstudium
Mathematik.
