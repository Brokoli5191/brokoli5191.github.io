<b>Allgemeines:</b> In der Analysis bzw. ihren Anwendungen stellt sich heraus, dass es in vielen F&auml;llen sinnvoll ist,
Funktionen als Elemente eines geeigneten (Vektor-)Raumes aufzufassen---also als Punkte in einem Funktionenraum. Ein Ihnen
bekanntes Beispiel ist vermutlich der Satz &uuml;ber die L&ouml;sbarkeit gew&ouml;hnlicher Differentialgleichungen (Picard-Lindel&ouml;f): 
hier wird die (eindeutige) L&ouml;sung als Fixpunkt einer Kontraktionsabbildung auf einem vollst&auml;ndigen metrischen Raum von 
stetigen Funktionen gewonnen. <br>

Die Funktionalanalysis ist diejenige Displizpin der Mathematik, in der Funktionen und Folgen (oder auch Folgen von Funktionen)
konsequent als Punkte in Vektorr&auml;umen aufgefasst und systematisch diese Vektorr&auml;ume und die Abbildungen zwischen ihnen studiert 
werden. Bei etwas genauerem Hinsehen bemerkt man, dass sich nur dann eine reichhaltige Theorie ergibt, wenn die 
enstprechenden Vektorr&auml;ume mit einer Topologie versehen werden: diese erlaubt es n&auml;mlich die Kernbegriffe der 
Analysis---Konvergenz und Stetigkeit---ins Spiel zu bringen. So gesehen ist die Funktionalanalysis das Studium dieser 
topologischen Vektorr&auml;ume.<br>

Etwas spezieller werden wir uns vor allem mit den Grundlagen der linearen, normierten Funktionalanalysis befassen, d.h. Vektorr&auml;ume,
die mit einer Norm, oder (noch spezieller) mit einem Skalarprodukt versehen sind, und die stetigen 
linearen Abbildungen zwischen ihnen studieren.<br>

Methodisch lebt die Funktionalanalysis von dem sich daraus ergebenden Zusammenspiel von analytischen und (linear-)algebraischen 
Techniken. Gewisserma&szlig;en k&ouml;nnte man die Funktionalanalysis als die Zusammenf&uuml;hrung der Analysis mit der linearen Algebra
sehen. <br><br>

Der Grundstein der Funktionalanalysis wurde zu Beginn des vorigen Jahrhunderts vor allem von David Hilbert, Erhard Schmidt
und Marcel Riesz gelegt. In den 1920er und 1930er Jahren wurde die Theorie---auch in 
Wechselwirkung mit der gerade enstehenden Quantenmechanik---von John von Neumann, sowie der "polnischen Schule", allen voran Stefan Banach in ihre heutige Form gebracht.
Heute sind funktionalanalytische Resultate grundlegend f&uuml;r viele Gebiete der Mathematik (Partielle Differentialgleichungen,
Numerik, Wahrscheinlichkeitstheorie, Approximationstheorie) und der theoretischen Physik (Quanten(feld)theorie).
<br><br>

<b>Inhalt:</b> Diese Vorlesung bietet eine Einf&uuml;hrung in die Funktionalanalysis mit den Schwerpunkten:
<ul>
<li>Banach- und Hilbertr&auml;ume: Grundlagen und Beispiele
<li>Normierte Vektorr&auml;ume und stetige lineare Abbildungen
<li>Hilbertr&auml;ume: Orthogonalit&auml;t und Orthonormalbasen
<li>Spektraltheorie kompakter Operatoren
<li>Haupts&auml;tze der Funktionalanalysis (Hahn-Banach, Banach-Steinhaus, offene Abbildung, abgeschlossener Graph)
</ul>
<br>

<b>Literatur:</b> In vielen Teilen der Vorlesung habe ich mich von den Büchern von John B. Conway (A Course in Functional Analysis, Second Edition, Springer Graduate Texts in Mathematics, Band 96, 1997) und von Dirk Werner (Funktionalanalysis, Springer, Berlin, 8. Auflage 2018) leiten lassen. Eine (etwas ältere) kommentierte Literaturliste findet 
sich <a href="fa_lit.pdf">hier</a>. 
<br><br>

