      <p>Nach der kurzen Einführungsphase wird die Lehrveranstaltung aufzeigen,
      wo im Mathematikstudium die Schwerpunkte gesetzt werden, wobei auf das Beweisen, typische
      mathematische Formulierungen und die korrekte Ausdrucksweise
      besonders Wert gelegt werden wird.</p>

      <p>Schließlich werden darüber hinaus einige Grundlagen vermittelt,
      die in den beiden anschließenden Vorlesungen <b>Analysis 1</b> und
      <b>Lineare Algebra 1</b> benötigt werden.</p>

      <p>Die Vorlesung ist eine Pflichtveranstaltung für alle Studenten
      des <a href="/Misc/Studienplan-LA.html">Lehramtes Mathematik</a> und ein 
      empfohlenes Freifach für die
      <a href="/Misc/Studienplan-Diplom.html">Mathematik-Diplom</a>-Studenten.
      Sie wird geblockt vom 1.10. bis zum 21.10. statt finden.</p>

      <p>Die Prüfungen werden schriftlich abgehalten und in Umfang und
      Schwierigkeit etwa einer Mathematik-Matura eines Realgymnasiums
      mit erweitertem Stoff entsprechen. In diesem Stoff sind sowohl die
      neu präsentierten Grundlagen als auch der Schulstoff enthalten.</p>
      
      <p>Typische Prüfungsbeispiele finden Sie
      <a href="http://www.mat.univie.ac.at/~herman/exams/einfprobe/probe.html">
       hier</a> (<a href="http://www.mat.univie.ac.at/~herman/exams/einfprobe.pdf">
      PDF</a> bzw. <a href="http://www.mat.univie.ac.at/~herman/exams/einfprobe.ps">
      PostScript</a> Version zum Ausdrucken)</p>

      <p><b>Wichtig:</b>
      Im Rahmen der Vorlesung erfolgt auch die Anmeldung zu den Proseminaren
