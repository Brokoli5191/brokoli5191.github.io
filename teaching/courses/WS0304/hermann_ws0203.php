      <h3>Informationen zur Lehrveranstaltung:</h3>

      <p>Am Beginn des Mathematikstudiums kämpfen die meisten Studenten mit den
      fundamentalen Unterschieden zwischen der Art wie Mathematik an den
      meisten Schulen gelehrt wird und dem hohen Maß an Exaktheit und
      Abstraktheit, das an der Universität verwendet wird.</p>
      
      <p>Manche Studenten, besonders am Lehramt interessierte, empfinden
      diesen fundamentalen Unterschied als Graben, an dessen Überwindung
      sie schon zu Beginn zu scheitern drohen. Diese Blockveranstaltung
      soll als Brücke zwischen der "Schul-" und der
      "Universitätsmathematik" dienen. Sie wird mit einer kurzen Zusammenfassung des benötigten 
      AHS-Stoffes beginnen, die dazu dienen soll die Studenten auf die notwendigen
      schulischen Grundkenntnisse hinzuweisen und ihnen Anhaltspunkte für
      etwaige noch bestehende Mängel zu geben. Zur Beseitigung dieser
      Mängel werden in den ersten drei bis vier Wochen <b>Intensiv-Workshops</b>
      organisiert. Die Teilnahme an diesen ist freiwillig und ist als
      Hilfestellung für all jene gedacht, die nicht das nötige Wissen
      aus der Schule mitgebracht haben. In diesem Jahr wird es die folgenden
      drei Workshops geben:</p>
      
      <ol>
       <li><b>Lineare Algebra:</b> Vektorrechnung, Grundlagen der Analytischen
       Geometrie, Kegelschnitte</li>
       <li><b>Analyis:</b> elementare Funktionen (Polynome, rationale Funktionen,
       log, exp, Winkelfunktionen,...), Stetigkeit, Differenzieren, Integrieren</li>
       <li><b>Wahrscheinlichkeitstheorie:</b> Abzählung, diskrete Verteilungen
       (Binomialverteilung, Poissonverteilung), kontinuierliche Verteilungen
       (Normalverteilung), Hypothesen</li>
      </ol>
      
      <p>Zusätzlich zum Schulstoff enthalten diese Workshops
      eine Einführung in die elektronischen Lehr- und Lernmedien zum
      Thema Mathematik und in das Software-Programm <tt>Mathematica</tt>.</p>

