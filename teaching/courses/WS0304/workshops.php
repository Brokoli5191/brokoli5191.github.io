      <h3>Methoden: Interaktivität und neue Medien.</h3>
      <p align="justify">
      Die Workshops weisen einen starken <em>interaktiven Charakter</em> auf, 
      der uns ein individuelles Eingehen auf die speziellen Bedürfnisse 
      und Fragen der TeilnehmerInnen ermöglicht. 
      </p>

      <p align="justify">
      Weiters kommen massiv <em>neue Medien</em> zum Einsatz: einerseits wird interaktive Lernsoftware
      (zb. <a href="http://www.mathe-online.at" target="_blank">mathe online</a>) zur 
      Unterstützung des Verstehens mathematischer Begriffsbildungen verwendet,
      andererseits wird das Computeralgebrasystem <a href="http://www.wolfram.com/products/mathematica/introduction.html" target="_blank">Mathematica</a> 
      eingesetzt, sowohl um konkrete Beispiele zu behandeln, als auch um die Anschauung 
      zu fördern.
      (Als Nebeneffekt kann ein sicherer Umgang mit Mathematica anhand
      bereits bekannter mathematischer Begriffe erlernt werden; diese
      Software wird auch im <a href="">Proseminar zu Analysis 1</a>
      verwendet.)
      </p>

      <p align="justify">
      Die Abhaltung der Workshops wird teilweise vom Projekt <a href="http://www.mathe-online.at/nml/" target="_blank">Neue Medien in der Mathematikausbildung</a> 
      unterstützt, das im Rahmen der Initiative <a href="http://www.nml.at/" target="_blank">Neue Medien in der Lehre an Universitäten und Fachhochschulen</a> 
      des Bundesministeriums für Bildung, Wissenschaft und Kultur finanziert wird.
      </p>

