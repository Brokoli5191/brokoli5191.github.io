      <h3>Allgemeines:</h3>
      <p align="justify"> 
      Eine gute Beherrschung des Schulstoffs ist Basis und Grundlage jedes
      erfolgreichen Mathematikstudiums. Unsere Erfahrung zeigt, dass die 
      Studienanfängerinnen diesbezüglich divergierende Voraussetzungen 
      und unterschiedlichstes Vorwissen mitbringen---je nach Schulform 
      und Qualität des Unterrichts.
      </p>

      <p align="justify">
      Um allen Studierenden die Möglichkeit zu bieten, hier an der Universität
      eine ausreichende Grundlage für ihr weiteres Studium zu erwerben, veranstalten
      wir in der <a href="http://www.mat.univie.ac.at/~ws0304#eingangsph">Studieneingangsphase</a> 
      ergänzend zur Vorlesung <a href="einfuehrung.php">Einführung in das mathematische Arbeiten</a> Workshops
      zur Aufarbeitung des Schulstoffs. Die Teilnahme an den Workshops ist 
      freiwillig---es handelt sich um keine "offizielle Lehrveranstaltung"!
      Auch eine teilweiser Besuch der Workshops (je nach spezifischem 
      Vorwissen bzw. zur Aufarbeitung spezifischer Defizite) ist sinnvoll
      und willkommen.
      </p>

      <h3>Ziel</h3>
      <p align="justify">
      der Workshops ist es, die für die Anfangssemester wichtigsten 
      Kapitel des Schulstoffs aufzubereiten bzw. vorhandene Verständnislücken
      der TeilnehmerInnen zu schließen und alle Studierenden in die
      Lage zu versetzen, erfolgreich die <a href="einfuehrung.php">Einführungsvorlesung</a> sowie die 
      AnfängerInnenvorlesungen <a href="">Analysis 1</a> und 
      <a href="">Lineare Algebra und Geometrie 1</a> zu absolvieren. (Bedenken Sie 
      auch, dass der Schulstoff zum Prüfungsstoff der Einführungsvorlesung zählt!)
      </p>

