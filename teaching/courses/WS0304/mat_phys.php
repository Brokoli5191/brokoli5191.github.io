      <p align="justify">
      Dieses Seminar ist als Fortsetzung der gleichnamigen Seminarserie von Gerhard Rein
      gedacht, richtet sich aber keineswegs ausschließlich an deren TeilnehmerInnen. In den
      letzten Semestern ist es "NeueinsteigerInnen" immer wieder gelungen,
      erfolgreich zum Seminar beizutragen.
      </p>

      <p align="justify">
      Wir werden Systeme nichtlinearer partieller Differentialgleichungen aus der 
      Mathematischen Physik insbesondere der kinetischen Theorie behandeln. Diese
      beschreibt das Verhalten von Teilchenensembles, die über ein
      kollektiv erzeugtes (zB. Gravitations- oder elektromagnetisches) Feld 
      miteinander wechselwirken. Beispiele für derartige Ensembles sind Galaxien
      oder Halbleiter; bei den Einzelteilchen handelt es sich dann um Sterne
      oder Elektronen und Ionen. Die physikalischen Modelle entstammen der Astrophysik, 
      der Kosmologie, der allgemeinen Relativitätstheorie und der Plasmaphysik.
      </p>

      <p align="justify">
      Die Verteilung der Teilchen im Phasenraum wird durch eine Dichtefunktion beschrieben,
      die einer Kontinuitätsgleichung genügt, die an die entsprechende Feldgleichung 
      gekoppelt wird. Das einfachste Beispiel eines solchen Systems ist die Vlasov-Poisson
      Gleichung, die die Zeitentwicklung einer Galaxie im Rahmen der Newton'schen 
      Gravitationstheorie oder eines Plasmas im Rahmen der Elektrostatik modelliert.
      </p>

      <p align="justify">
      Die engere Themenauswahl richtet sich selbstverständlich nach den 
      spezielle Interessen der TeilnehmerInnen und wird bei der Vorbesprechung getroffen.
      </p>

