      <h2 class="h3 article-title">Verallgemeinerte Funktionen</h2>
      
      <p><b>803795 SE Seminar (Analysis), 2 std, Fr. 12:30-14:00, Zi. 203, Währingerst. 17, 2. Stock</b></p>
      <p><a href="http://www.mat.univie.ac.at/~mike">Ao. Prof. Dr. Michael Kunzinger</a>,
      <a href="http://www.mat.univie.ac.at/~stein">Ao. Prof. Dr. Roland Steinbauer</a></p>
      
      <p align="justify">
      Dieses Seminar bietet eine Einführung in aktuelle Entwicklungen auf dem Gebiet der 
      <em>Algebren verallgemeinerter Funktionen (Colombeau Algebren)</em>, das in den letzten Jahren 
      entscheidend von der Forschungsgruppe <a href="http://diana.mat.univie.ac.at">DIANA (DIfferential Algebras and Nonlinear Analysis</a> in Wien und Innsbruck mitgeprägt wurde. 
      </p>

      <p align="justify">
      Algebren verallgemeinerter Funktionen nach J. F. Colombeau sind Differentialalgebren, die
      den Vektorraum der Distributionen als Teilraum enthalten und gleichzeitig maximale 
      Konsitstenzeigenschaften mit der klassichen Analysis aufweisen (im präzisen Sinne des 
      Unmöglichkeitsresultats von L. Schwartz).
      </p>

      <p align="justify">
      Dieses Seminar wendet sich an Studierende im 2. Studienabschnitt. Vorausgesetzt 
      werden solide Analysiskenntnisse; die Kenntnis der Vorlesung "Ausgewählte Kapitel aus 
      Moderne Analysis: Colombeau - Algebren" von Michael Grosser im vergangenen Sommersemester
      ist wünschenswert aber keinesfalls notwendige Voraussetzung für eine erfolgreiche Teilnahme.
      </p>

      <p align="justify">
      Die engere Auswahl an Themen richtet sich nach den Wünschen und Vorkenntnissen der TeilnehmerInnen.
      Mögliche Schwerpunkte sind unter anderem:
      </p>

      <ul>
       <li>Punktwertecharakterisierung verallgemeinerter Funktionen</li>
       <li>Anwendungen auf Differentialgleichungen</li>
       <li>Colombeau Algebren auf Mannigfaltigkeiten</li>
      </ul>

      <p align="justify">
      Die StudentInnen sollen sich dem Stoff anhand selbst vorbereiteter Seminarvortäge nähern. 
      Neben dem fachlichen Inhalt des Seminars ist uns vor allem die Vermittlung von 
      Präsentationstechnik und fachlicher Diskussionskultur ein großes Anliegen, das in 
      diesem Seminar sicher nicht zu kurz kommen wird. 
      </p>

      <p align="justify">
      Das Seminar kann auch als Einstieg in ein Diplomarbeitsthema dienen.
      </p>

      <p align="justify">
      <b>Literatur:</b> M. Grosser, M. Kunzinger, M. Oberguggenberger, R. Steinbauer,
      Geometric Theory of Generalized Functions (Kluwer, Dordrecht, 2001).
      </p>

      <p align="justify">
      <b>Zielpublikum:</b> DiplomstudentInnen der Mathematik und (theoretischen) Physik, LehramtsstudentInnen der Mathematik.
      </p>

      <p align="justify">
      <b>Positionierung im Studienplan:</b> Diplomstudium Mathematik, 2. Abschnitt,
      Studienschwerpunkt Analysis.
      </p>

      <p align="justify">
      <b>Beginn:</b> Vorbesprechung Fr, 10.10. 13:15, Hs. 4.
