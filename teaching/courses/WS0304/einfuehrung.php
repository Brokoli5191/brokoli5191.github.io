      <h3>Allgemeines: Mittel- und Hochschule.</h3>
      <p align="justify">Die Art und Weise wie Mathematik an Allgemeinbildenden Höheren Schulen
      unterrichtet wird unterscheidet sich fundamental von der Art und Weise
      wie Mathematik an Universitäten gelehrt wird, dh. von der Mathematik als 
      Wissenschaft.</p>

      <p align="justify">Während in der Schulmathematik meist das Lösen von Beispielen
      (bzw. das Erlernen von Schemata zur Lösung von Standardproblemen) im 
      Vordergrund steht, beschäftigt sich Mathematik als Wissenschaft 
      hauptsächlich mit abstrakten Strukturen. Diese werden durch möglichst wenige 
      grundlegende Attribute <em>definiert</em>; weitere gültige Eigenschaften
      sowie Querbeziehungen zu anderen Strukturen werden in
      <em>Beweisen</em> mittels logischer Schlußfolgerungen aus diesen
      Grundlagen und bereits bekannten Tatsachen abgeleitet. Beispiele
      dienen primär zur Illustration abstrakter Sachverhalte.</p>

      <p align="justify">
      So gibt es wohl kaum ein Fach, bei dem ein tieferer und breiterer 
      Graben zwischen Schule und Hochschule zu überwinden ist, und viele
      StudienanfängerInnen---besonders am Lehramt interessierte---drohen 
      bereits in den ersten Wochen an diesem Übergang zu scheitern. 
      Um dieser Problematik sinnvoll zu begegnen 
      und den Studierenden den Einstieg in die Hochschulmathematik zu 
      erleichtern, wurde vor einigen Jahren am <a href="http://www.mat.univie.ac.at">Institut für Mathematik</a> der <a href="http://www.univie.ac.at">Universität Wien</a> eine <a href="http://www.mat.univie.ac.at/~ws0304#eingangsph">Studieneingangsphase</a> eingeführt, deren Herzstück diese Einführungsvorlesung darstellt.
      </p>

      <h3>Inhalte:</h3>
      <ul>
       <li>Einleitung: Hürden zu Studienbeginn, Schul- und Universitätsmathematik.</li>
       <li>Grundlagen: Beweise, Schreibweisen (Indizes, Summen- und Produktzeichen),
        Gleichungsumformungen (Stil und Fallen), Vollständige Induktion.</li>
       <li>Logik, Mengenlehre: Boolsche Algebren, Aussagen, Logik, Mengen,
        Abbildungen.</li>
       <li>Algebra: Gruppen, Ringe, Körper.</li>
       <li>Zahlenmengen: natürliche, ganze, rationale, reelle und komplexe Zahlen.</li>
       <li>Computeralgebrasysteme: Kurzeinführung in Mathematica.</li>
      </ul>
      
