              <b>Allgemeines:</b>
              Die Theorie der Distributionen---oft auch verallgemeinterte Funktionen genannt---ist eine Erweiterung der klassischen Analysis, die Mitte des 20. Jahrhunderts vor allem von Laurent
              Schwartz und Sergei Sobolev entwickelt wurde.<br>

              In der Physik und den Ingenieurwissenschaften waren verallgemeinerte Funktionen-Ideen in mehr oder weniger vager und exakter Form schon lange und weit verbreitet (Kirchhoff 1882,
              Heaviside 1898, Dirac 1926) aber erst die elegante (funktionalanalytische) Formulierung von Laurent Schwartz (um 1945) brachte den ganz grossen Erfolg. Binnen k&uuml;rzester Zeit nahm
              die Theorie der Distributionen sowohl innerhalb der Funktionalanalysis, als auch in Anwendungen (partielle Differentialgleichungen, theoretische Physik) einen zentralen Platz ein.
              <br>
              Die Grundidee dieser Verallgemeinerung des Funktionsbegriffs ist es, statt der ''klassischen'' Zuordnung ${\mathbb R} \ni x \mapsto f(x)$ die Zuordnung $\varphi \mapsto \int f(x)
              \varphi(x) dx$ zu betrachten, wobei $\varphi$ Element eines geeigneten Funktionenraums $\cal{D}$ ist; verallgemeinerte Funktionen sind also lineare, (stetige) Funktionale auf ${\cal D}$.
              Dieser Begriffsrahmen ist besonders gut geeignet, um f&uuml;r (klassisch) nichtdifferenzierbare oder sogar unstetige Funktionen (etwa die Heaviside'sche Sprungfunktion oder die
              Dirac'sche Deltafunktion) einen Ableitungsbegriff zu entwickeln und eine reichhaltige Theorie verallgemeinerter Funktionen zu erm&ouml;glichen.
              <br>
              Obwohl die gr&ouml;&szlig;te Triebkraft hinter der Entwicklung der Distributionentheore der Wunsch nach einer Erweiterung von L&ouml;sungskonzepten f&uuml;r partielle
              Differentialgleichungen (schwache L&ouml;sungen, Fundamentall&ouml;sungen) war, erreichten die revolution&auml;ren Einwirkungen auf die Analysis von (linearen) partiellen
              Differentialoperatoren insbesondere durch L. H&ouml;rmander (seit Mitte der 1950er Jahre) unvorhergesehene Ausma&szlig;e.

              <br><br>

              <b>Inhalt:</b> In der Vorlesung wird die Theorie der Distributionen auf ''elementarem'' Niveau (also ohne Einbeziehung der Theorie lokalkonvexer Vektorr&auml,ume) entwickelt;
              gem&auml,&szlig; der Rolle der Distributionentheorie als "analytische Technologie".
              <br>
              Als Hauptreferenz wird uns das Buch von F.G. Friedlander und M. Joshi ("Introduction to the Theory of Distributions", 2nd Edition, Cambridge Universtiy Press, 1998) dienen. Kerninhalte
              f&uuml;r etwa 2/3 der Vorlesung sind
