<p align="justify"><a name="allg"><b>Allgemeines:</b> Mittel- und Hochschule.</a>
Die Art und Weise wie Mathematik an Allgemeinbildenden H&ouml;heren Schulen
unterrichtet wird unterscheidet sich fundamental von der Art und Weise
wie Mathematik an Universit&auml;ten gelehrt wird, dh. von der Mathematik als 
Wissenschaft.

W&auml;hrend in der Schulmathematik meist das L&ouml;sen von Beispielen
(bzw. das Erlernen von Schemata zur L&ouml;sung von Standardproblemen) im 
Vordergrund steht, besch&auml;ftigt sich Mathematik als Wissenschaft 
haupts&auml;chlich mit abstrakten Strukturen. Diese werden durch m&ouml;glichst wenige 
grundlegende Attribute <em>definiert</em>; weitere g&uuml;ltige Eigenschaften
sowie Querbeziehungen zu anderen Strukturen werden in
<em>Beweisen</em> mittels logischer Schlu&szlig;folgerungen aus diesen
Grundlagen und bereits bekannten Tatsachen abgeleitet. Beispiele
dienen prim&auml;r zur Illustration abstrakter Sachverhalte.
</p>

<p align="justify">
So gibt es wohl kaum ein Fach, bei dem ein tieferer und breiterer 
Graben zwischen Schule und Hochschule zu &uuml;berwinden ist, und viele
Studienanf&auml;ngerInnen---besonders am Lehramt interessierte---drohen 
bereits in den ersten Wochen an diesem &Uuml;bergang zu scheitern. 
Um dieser Problematik sinnvoll zu begegnen 
und den Studierenden den Einstieg in die Hochschulmathematik zu 
erleichtern, wurde vor einigen Jahren an der
<a href="http://www.mat.univie.ac.at">Fakult&auml;t f&uuml;r Mathematik</a> der 
<a href="http://www.univie.ac.at">Universit&auml;t Wien</a> eine 
<a href="http://www.mat.univie.ac.at/~ws0405#eingangsph">Studieneingangsphase</a> 
eingef&uuml;hrt, deren Herzst&uuml;ck diese Einf&uuml;hrungsvorlesung darstellt.
</p>

<p align="justify"><b>Studieneingangsphase:</b> Basis Schulstoff. 
Die Erfahrung zeigt, dass die angehenden MathematikstudentInnen---je
nach Schulform und Qualit&auml;t des Unterrichts---sehr unterschiedliches 
fachliches Vorwissen f&uuml;r ihr Studium mitbringen. Es ist eine der 
prim&auml;ren Aufgaben der Studieneingangsphase, die wichtigsten Voraussetzungen 
"offiziell" zu machen und eine gemeinsame Basis f&uuml;r ein erfolgreiches 
Mathematikstudium zu legen---aber auch L&uuml;cken aus der Schulzeit aufzudecken
mit dem Ziel, diese m&ouml;glichst rasch zu schlie&szlig;en. Um den Studierenden
eine optimale M&ouml;glichkeit daf&uuml;r zu bieten, werden w&auml;hrend der 
Studieneingangsphase zus&auml;tzlich zur Einf&uuml;hrungsvorlesung 
<a href="http://www.mat.univie.ac.at/~ws0405/workshops.php">Workshops</a> 
angeboten, in denen die wichtigsten
Aspekte des Schulstoffs aufgearbeitet werden. Die Teilnahme an den Workshops,
die von TutorInnen unter Einsatz neuer Medien gestaltet werden, 
ist freiwillig, wird aber sehr empfohlen. Bedenken Sie, dass die Mathematik 
eine gr&ouml;&szlig;tenteils "aufbauende" Wissenschaft ist und daher der Sicherung 
der Basis besondere Bedeutung zukommt: Diese ist eine der wichtigsten 
Voraussetzung f&uuml;r den sp&auml;teren Studienerfolg!
</p>

<p align="justify"><b>Anf&auml;ngerInnenprobleme:</b> Einsch&auml;tzung und L&ouml;sung.
Erfahrungsgem&auml;&szlig; sind nicht die Probleme in der Anfangsphase des Studiums entscheidend f&uuml;r den weiteren Studienerfolg, 
sondern vielmehr <em>die Art und Weise wie diese Schwierigkeiten umgangen werden</em>---insbesondere wie frau/man sich einem unbekannten Stoff n&auml;hert und wie man/frau auf auftauchende Probleme reagiert und diese meistert. Insoferne sollen---im weitesten Sinne---auch Lerntechniken, Sichtweisen, Konventionen, allgemeine Richtlinien, "Selbstverst&auml;ndlichkeiten" etc. im Zuge dieser Lehrveranstaltung angesprochen werden.
</p>

<p align="justify"><b>Ziel der Lehrveranstaltung</b> ist es, eine solide Grundlage 
f&uuml;r die nach der Studieneingangsphase beginnenden Anf&auml;ngerInnenvorlesungen
<a href="http://data.univie.ac.at/vlvz?semester=current&lvnr=801460" target="_blank">Analysis 1</a> sowie <a href="http://data.univie.ac.at/vlvz?semester=current&lvnr=803776" target="_blank">Lineare Algebra und Geometrie 1</a>
und das weitere Mathematikstudium zu schaffen. 
Neben der direkten Vermittlung der 
<a href="#inhalt">Inhalte</a> wird besonderer Wert auf das Erlernen 
mathematischer Sprech- und Schreibweisen, sowie typischer 
mathematischer Formulierungen und korrekter Ausdrucksweisen gelegt.
Dar&uuml;berhinaus wird in dieser Lehrveranstaltung aufgezeigt, wo im 
Mathematikstudium Schwerpunkte gesetzt werden.
</p>

<p align="justify"><b>Inhalte:</b><a name="inhalt"></a>
<ul>
 <li>Einleitung: H&uuml;rden zu Studienbeginn, Schul- und Universit&auml;tsmathematik.
 <li>Grundlagen: Beweise, Schreibweisen (Indizes, Summen- und Produktzeichen),
	Gleichungsumformungen (Stil und Fallen), Vollst&auml;ndige Induktion.
 <li>Logik: Boolsche Algebren, Aussagenlogik.
 <li>Mengenlehre: Mengen, Relationen, Abbildungen.
 <li>Algebra: Gruppen, Ringe, K&ouml;rper.
 <li>Zahlenmengen: nat&uuml;rliche, ganze, rationale, reelle und komplexe Zahlen.
 </ul>


