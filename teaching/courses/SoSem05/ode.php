      <h3>Allgemeines:</h3>
      <p>
        Was auch immer Sie modellieren wollen, ob in der Physik, der Biologie, der Technik, oder den Wirtschaftswissenschaften, sehr oft wird das Modell die Veränderung einer Größe in Abhängigkeit von
        eben dieser Größe selbst (und eventuell auch noch anderen Größen) beschreiben. Solche Modellierungen führen auf Differentialgleichungen, also Gleichungen zwischen einer Funktion und ihren
        Ableitungen; unter gewöhnlichen Differentialgleichungen verstehen wir solche, wo die gesuchte Funktion nur von einer Variablen abhängt, also wir es "nur" mit ihren ("gewöhnlichen") Ableitungen
        zu tun bekommen---und nicht etwa mit den partiellen Ableitungen einer Funktion in mehreren Veränderlichen. [Letzteres würde uns in die Welt partiellen Differentialgleichungen führen, eines der
        wichtigsten Gebiete der angewandten Mathematik, aber das ist eine andere Geschichte...]
      </p>

      <h3>Inhalt:</h3>
      <p>
        Ziel der Untersuchung von Differentialgleichungen ist es, so viel wie möglich über die Lösung(en) einer Gleichung (oder ganzer Typen von Gleichungen) heraus zu finden, also über jene
        Funktion(en), die die gegebene Beziehung zwischen Funktion und Ableitungen erfüllt. Während früher der Fokus auf das explizite Finden ("Ausrechnen") von Lösungen spezieller (Typen von)
        Gleichungen gelegt wurde, beginnt die moderne Sichtweise bei der Erkenntnis, das dieses explizite Lösen bei der überwigenden Mehrzahl der Gleichungen ohnehin nicht möglich ist; stattdesssen
        wird das qualitative Verhalten der Lösungen studiert, ohne diese explizit zu kennen---ja kennen zu müssen. Wichtig dabei ist es allerdings zu wissen, dass eine Lösung überhaupt existiert und
        dass diese eindeutig ist. Das ist zum Glück lokal fast immer so, wie uns die klassische Existentztheorie von gewöhnlichen Differentialgleichungen lehrt.
      </p>

      <p>
        Dementsprechend werden wir uns in der Vorlesung nach einer Einführung, in der anhand von Beispielen die grundlegenden Probleme, Begriffe und Methoden aufgezeigt werden und nach einem
        gründlichen Studium der Existenztheorie (Existenzsatz von Picard-Lindelöf, Stetige Abhängigkeit von den Daten, Fortsetzbarkeit von Lösungen, Fluss einer Differentialgleichung) auf die Anfänge
        der Qualitativen Theorie der gewöhnlichen Differentilagleichungen stürzen. Insbesondere werden wir 2-dimensionale Systeme linearer Differentialgleichungen studieren.
      </p>

