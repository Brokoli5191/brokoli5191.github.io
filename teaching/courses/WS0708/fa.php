              <h3>Allgemeines:</h3>
              <p align="justify">In der Analysis bzw. ihren Anwendungen stellt sich heraus, dass es in vielen Fällen sinnvoll ist,
              Funktionen als Elemente eines geeigneten (Vektor-)Raumes aufzufassen---also als Punkte in einem Funktionenraum. Ein Ihnen
              bekanntes Beispiel ist vermutlich der Satz über die Lösbarkeit gewöhnlicher Differentialgleichungen (Picard-Lindelöf): 
              hier wird die (eindeutige) Lösung als Fixpunkt einer Kontraktionsabbildung auf einem vollständigen metrischen Raum von 
              stetigen Funktionen gewonnen.</p>
              
              <p>Die Funktionalanalysis ist diejenige Displizpin der Mathematik, in der Funktionen und Folgen (oder auch Folgen von Funktionen)
              konsequent als Punkte in Vektorräumen aufgefasst und systematisch diese Vektorräume und die Abbildungen zwischen ihnen studiert 
              werden. Bei etwas genauerem Hinsehen bemerkt frau/man, dass sich nur dann eine reichhaltige Theorie ergibt, wenn die 
              enstprechenden Vektorräume mit einer Topologie versehen werden: diese erlaubt es nämlich die Kernbegriffe der 
              Analysis---Konvergenz und Stetigkeit---ins Spiel zu bringen. So gesehen ist die Funktionalanalysis das Studium dieser 
              topologischen Vektorräume.</p>
              
              <p>Etwas spezieller werden wir uns mit den Grundlagen der linearen, normierten Funktionalanalysis befassen, d.h. Vektorräume,
              die mit einer Norm, oder (noch spezieller) mit einem Skalarprodukt versehen sind, und die stetigen 
              linearen Abbildungen zwischen ihnen studieren.</p>
              
              <p>Methodisch lebt die Funktionalanalysis von dem sich daraus ergebenden Zusammenspiel von analytischen und (linear-)algebraischen 
              Techniken. Gewissermaßen könnte man/frau die Funktionalanalysis als die Zusammenführung der Analysis mit der linearen Algebra
              sehen.</p>
              
              <p>Der Grundstein der Funktionalanalysis wurde zu Beginn des vorigen Jahrhunderts vor allem von David Hilbert, Erhard Schmidt
              und Marcel Riesz gelegt. In den 1920er und 1930er Jahren wurde die Theorie---auch in 
              Wechselwirkung mit der gerade enstehenden Quantenmechanik---von John von Neumann, sowie der "polnischen Schule", allen voran Stefan Banach in ihre heutige Form gebracht.
              Heute sind funktionalanalytische Resultate grundlegend für viele Gebiete der Mathematik (Partielle Differentialgleichungen,
              Numerik, Wahrscheinlichkeitstheorie, Approximationstheorie) und der theoretischen Physik (Quanten(feld)theorie).</p>
              
              <h3>Inhalt:</h3>
              <p>Diese Vorlesung bietet eine ausgewogene Einführung in die Funktionalanalysis. Sie soll den
              Studierenden (vor allem des Studienschwerpunkts "Analysis") die gemeinsamen Grundlagen vermitteln, die eine weitere Spezialisierung in den verschiedenen an der 
              Fakultät vertretenen Arbeitsrichtungen ermöglicht. 
              Etwas detaillierter gliedert sich der Inhalt wie folgt:</p>
              <ul>
                <li>Banach- und Hilberträume: Grundlagen und Beispiele</li>
                <li>Normierte Vektorräume und stetige lineare Abbildungen</li>
                <li>Hilberträume: Orthogonalität und Orthonormalbasen</li>
                <li>Hauptsätze der Funktionalanalysis (Hahn-Banach, Banach-Steinhaus, offene Abbildung, abgeschlossener Graph)</li>
              </ul>
              
