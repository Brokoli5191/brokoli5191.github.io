<b>Allgemeines:</b> Die mengentheoretische Topologie---mit <i>deren</i> Grundlagen
besch&auml;ftigt sich diese Vorlesung---ist in erster Linie eine innermathematische
Grundlagendisziplin. Sie befasst sich in gro&szlig;er Allgemeinheit mit allem, was 
auch nur entfernt mit den Begriffen von "N&auml;he", "Umgebung" und "Konvergenz" 
zu tun hat. Ihre Kraft im Kontext anderer 
mathematsicher Gebiete beruht vor allem auf ihrem vereinheitlichenden 
Begriffssystem und ihrer gl&uuml;cklichen Terminologie. Insbesondere
stellt sie einen Begriffsapparat zur Verf&uuml;gung, der es erlaubt in vielen sehr
abstrakten Situationen einen Anschluss an unser r&auml;umliches 
Vorstellungsverm&ouml;gen zu finden.
<br><br>

<b>Inhalt:</b> Wir bauen auf den einschl&auml;gigen Kenntnissen aus den 
Vorlesungen zur Analysis auf, wo ja bereits Konvergenz, Stetigkeit, offene 
und abgeschlossene Mengen sowie Kompaktheit eine tragende Rolle gespielt haben. 
Wir widmen uns dem allgemeinen Rahmen  f&uuml;r derartiger Konzepte n&auml;mlich den 
Begriffen metrischer und (in erster Linie) topologischer Raum sowie dem Studium der 
Kernbegriffe TC^3---manchmal auch TC^4:  topology; [convergence,] continuity, 
compactness, connectedness (Toplogie; [Konvergenz,] Stetigkeit, Kompaktheit, 
Zusammenhang)---in topologischen R&auml;umen. 
<br><br>


<b>Literatur:</b> Eine kommentierte Literaturliste finden Sie 
<a href="top_literatur_2015.pdf">hier</a>.
<br><br>

<b>Ziele:</b> Neben den unmittelbaren inhaltlichen Zielen der LVA,
d.h. einer guten Vorbereitung auf all die weiterf&uuml;hrenden Lehrveranstaltungen 
in denen topologische Begriffe eine tragende Rolle spielen (Funktionalanalysis, Differentialgeometrie), ist es mir wichtig, dem abstrakten Charakter des Gebiets 
Rechnung zu tragen und Ihr Verst&auml;ndnis sowie Ihre Freude an mathematischen 
Abstraktionen und Begriffsbildungen (weiter) zu entwickeln. 
<br><br>

<b>Voraussetzung</b> zum erfolgreichen Besuch der Lehrveranstaltung ist
typischerweise die Kenntnis der Grundvorlesungszyklen aus Analysis und 
linearer Algebra.
<br><br>

<B>Zielpublikum:</B> Prinzipiell alle, die am Thema Interesse und 
Freude haben, insbesondere Bachelorstudierende der Mathematik aber etwa auch
Bachelorstudierende der Physik, sowie Lehramtsstudierende dieser beiden 
Studienrichtungen.
<br><br>

<B>Positionierung im Curriculum:</B>
Teil des Pflichtmoduls Höhere Analysis (HAN) im Bachelortudium Mathematik.
