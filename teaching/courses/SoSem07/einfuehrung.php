<p align="justify">
  <a name="allg"><b>Allgemeines:</b> Mittel- und Hochschule.</a> Die Art und Weise wie Mathematik an Allgemeinbildenden H&ouml;heren Schulen unterrichtet wird, unterscheidet sich fundamental von der
  Art und Weise wie Mathematik an Universit&auml;ten gelehrt wird, dh. von der Mathematik als Wissenschaft. W&auml;hrend in der Schulmathematik meist das L&ouml;sen von Beispielen (bzw. das Erlernen
  von Schemata zur L&ouml;sung von Standardproblemen) im Vordergrund steht, besch&auml;ftigt sich Mathematik als Wissenschaft haupts&auml;chlich mit abstrakten Strukturen. Diese werden durch
  m&ouml;glichst wenige grundlegende Attribute <em>definiert</em>; weitere g&uuml;ltige Eigenschaften sowie Querbeziehungen zu anderen Strukturen werden in <em>Beweisen</em> mittels logischer
  Schlu&szlig;folgerungen aus diesen Grundlagen und bereits bekannten Tatsachen abgeleitet. Beispiele dienen prim&auml;r zur Illustration abstrakter Sachverhalte.
</p>

<p align="justify">
  So gibt es wohl kaum ein Fach, bei dem ein tieferer und breiterer Graben zwischen Schule und Hochschule zu &uuml;berwinden ist, und viele Studienanf&auml;ngerInnen---besonders am Lehramt
  interessierte---drohen bereits in den ersten Wochen an diesem &Uuml;bergang zu scheitern. Um dieser Problematik sinnvoll zu begegnen und den Studierenden den Einstieg in die Hochschulmathematik zu
  erleichtern, wurde vor einigen Jahren an der
  <a href="http://www.mat.univie.ac.at">Fakult&auml;t f&uuml;r Mathematik</a> der <a href="http://www.univie.ac.at">Universit&auml;t Wien</a> eine
  <a href="http://www.mat.univie.ac.at/~sosem07#eingangsph">Einf&uuml;hrungsphase</a>
  eingef&uuml;hrt, deren Herzst&uuml;ck diese Einf&uuml;hrungsvorlesung darstellt.
</p>

<p align="justify">
  <b>Einf&uuml;hrungsphase:</b> Basis Schulstoff. Die Erfahrung zeigt, dass die angehenden MathematikstudentInnen---je nach Schulform und Qualit&auml;t des Unterrichts---sehr unterschiedliches
  fachliches Vorwissen f&uuml;r ihr Studium mitbringen. Es ist eine der prim&auml;ren Aufgaben der Einf&uuml;hrungsphase, die wichtigsten Voraussetzungen "offiziell" zu machen und eine gemeinsame
  Basis f&uuml;r ein erfolgreiches Mathematikstudium zu legen---aber auch L&uuml;cken aus der Schulzeit aufzudecken mit dem Ziel, diese m&ouml;glichst rasch zu schlie&szlig;en. Um den Studierenden
  eine optimale M&ouml;glichkeit daf&uuml;r zu bieten, werden w&auml;hrend der Einf&uuml;hrungsphase zus&auml;tzlich zur Einf&uuml;hrungsvorlesung
  <a href="http://www.mat.univie.ac.at/~evelina/workshops.php">Workshops</a>
  angeboten, in denen die wichtigsten Aspekte des Schulstoffs aufgearbeitet werden. Die Teilnahme an den Workshops, die von Tutorinnen in interaktiver Weise gestaltet werden, ist freiwillig, wird aber
  sehr empfohlen. Bedenken Sie, dass die Mathematik eine gr&ouml;&szlig;tenteils "aufbauende" Wissenschaft ist und daher der Sicherung der Basis besondere Bedeutung zukommt: Diese ist eine der
  wichtigsten Voraussetzung f&uuml;r den sp&auml;teren Studienerfolg!
</p>

<p align="justify">
  <b>Anf&auml;ngerInnenprobleme:</b> Einsch&auml;tzung und L&ouml;sung. Erfahrungsgem&auml;&szlig; sind nicht die Probleme in der Anfangsphase des Studiums entscheidend f&uuml;r den weiteren
  Studienerfolg, sondern vielmehr <em>die Art und Weise wie diese Schwierigkeiten umgangen werden</em>---insbesondere wie frau/man sich einem unbekannten Stoff n&auml;hert und wie man/frau auf
  auftauchende Probleme reagiert und diese meistert. Insoferne sollen---im weitesten Sinne---auch Lerntechniken, Sichtweisen, Konventionen, allgemeine Richtlinien, "Selbstverst&auml;ndlichkeiten" etc.
  im Zuge dieser Lehrveranstaltung angesprochen werden.
</p>

<p align="justify">
  <b>Ziel der Lehrveranstaltung</b> ist es, eine solide Grundlage f&uuml;r die nach der Einf&uuml;hrungsphase beginnenden Anf&auml;ngerInnenvorlesungen
  <a href="http://online.univie.ac.at/vlvz?lvnr=250351&semester=S2007" target="_blank">Analysis 1</a> sowie
  <a href="http://online.univie.ac.at/vlvz?lvnr=250360&semester=S2007" target="_blank">Lineare Algebra und Geometrie 1</a>
  und das weitere Mathematikstudium zu schaffen. Neben der direkten Vermittlung der
  <a href="#inhalt">Inhalte</a> wird besonderer Wert auf das Erlernen mathematischer Sprech- und Schreibweisen, sowie typischer mathematischer Formulierungen und korrekter Ausdrucksweisen gelegt.
  Dar&uuml;berhinaus wird in dieser Lehrveranstaltung aufgezeigt, wo im Mathematikstudium Schwerpunkte gesetzt werden.
</p>

<p align="justify"><b>Inhalte:</b><a name="inhalt"></a></p>
<ul>
  <li>Einleitung: H&uuml;rden zu Studienbeginn, Schul- und Universit&auml;tsmathematik.</li>
  <li>Grundlagen: Beweise, Schreibweisen (Indizes, Summen- und Produktzeichen), Gleichungsumformungen (Stil und Fallen), Vollst&auml;ndige Induktion.</li>

  <li>Logik: Boolsche Algebren, Aussagenlogik.</li>
  <li>Mengenlehre: Mengen, Relationen, Abbildungen.</li>
  <li>Algebra: Gruppen, Ringe, K&ouml;rper.</li>
  <li>
    Zahlenmengen: nat&uuml;rliche, ganze, rationale, reelle und komplexe Zahlen.

    <p align="justify">
      <b>Skriptum und Literatur:</b> Als schriftliche Grundlage der Vorlesung dient das Skriptum von <a href="http://www.mat.univie.ac.at/~herman" target="_blank">Hermann Schichl</a>/<a
        href="http://www.mat.univie.ac.at/~stein"
        target="_blank"
        >Roland Steinbauer</a
      >. Es kann <a href="einfuehrung_sosem07.pdf"> hier</a> downgeloaded oder ab Ende Februar im <a href="http://www.mat.univie.ac.at/studentinfo/lecturenotes/">Skriptenverkauf</a> der Fakult&auml;t
      erworben werden. (Es handelt sich um einen unver&auml;nderten Nachdruck der Version vom Wintersemester 2005/06.)
    </p>

    <p align="justify">
      Falls Sie noch vor Semesterbeginn Lust versp&uuml;ren, sich auf Ihr Studium einzustimmen, sei Ihnen das erste Kapitel des Buchs
      <a href="http://www.amazon.de/exec/obidos/ASIN/3528031999/qid=1059574913/sr=2-1/ref=sr_aps_prod_1_1/028-5436438-5246119" target="_blank"><em>Analysis, Band 1.</em></a> von Ehrhard Behrends
      (Vieweg, 2003), sowie die ausgezeichnete Einf&uuml;hrung in die mathematische "Formulierkunst"
      <a href="http://www.amazon.de/exec/obidos/ASIN/3528564423/ref=pd_sr_ec_ir_b/028-5436438-5246119" target="_blank"><em>Das ist o.B.d.A. trivial.</em></a> von Albrecht Beutelspacher (Vieweg, 1999)
      empfohlen. Weitere Lernbehelfe sowie Literaturhinweise werden in der Vorlesung bekanntgegeben.
    </p>

    <p align="justify">
      <b>Begleitende Lehrveranstaltungen:</b>
      Die Proseminare zu den Anf&auml;ngerInnenvorlesungen, dh. das
