<p align="justify">
<b>Allgemeines:</b> Die Geometrie ist jenes Teilgebiet der Mathematik, dass sich mit Fragen der Form, Größe und Position von Figuren und den Eigenschaften des Raumes besch&auml;ftigt. Sie ist eine der &auml;ltesten Wissenschaften &uuml;berhaupt. Neben den bereits den Griechen (und aus der Schule) bekannten synthetischen (=konstruktiven) Methoden werden seit Mitte des 19. Jahrhunderts verstärkt analytische Methoden zur Untersuchung geometrischer Objekte verwendet. Diese Differentialgeometrie geht vor allem auf Arbeiten von Carl Friedrich Gauss &uuml;ber die intrinsiche Geometrie von Fl&auml;chen zur&uuml;ck sowie auf Arbeiten von Bernhard Riemann, der dem Begriff der Kr&uuml;mmung von Fl&auml;chen seine moderne Formulierung gab.
</p>


<table align="left">
<td width="80%">
<p align="justify">
<b>Inhalt:</b> In diesem Seminar werden wir uns im Rahmen der <em>elementaren Differentialgeometrie</em> mit einigen Grundbegriffen der modernen Differentialgeometrie und insbesondere mit dem Begriff der <em>Kr&uuml;mmung</em> besch&auml;ftigen. Hier bedeutet ,,elementar'' nicht etwa, dass es sich um besonders einfache Sachverhalte handelt, sondern, dass diese ohne Verwendung des umfassenden formalen Begriffsappartats der Differentialgeometrie (Mannigfaltigkeiten, B&uuml;ndel, Schnitte, etc.) beschrieben werden.
<br><br>
Die konkreten Inhalte sind nahe am Seminar <a href="../WS1415/semlak.html">"Die Geometrie von Kurven und Ebenen"</a> aus dem Wintersemesetr 2014/15 angesiedelt bzw. als dessen Fortsetzung konzipiert. Wir werden uns vor allem mit regul&auml;ren Fl&auml;chen besch&auml;ftigen -- das sind "sch&ouml;ne" 2-dimensionale Fl&auml;chen im 3-dimensionalen Euklidischen Raum R^3 -- und ihrer sogenannten inneren Geometrie. Damit sind grob gesprochen alle geometrischen Eigenschaften und Objekte gemeint, die "2-dimensionale Bewohner" der Fl&auml;che (sog. Flachl&auml;nder) messen k&ouml;nnen, die nichts vom umgebenden 3-dimesionalen Raum wissen. Ein Beispiel sind etwa die k&uuml;rzesten Verbindungslinien zwischen 2 Punkten der Fl&auml;che, die innerhalb der Fl&auml;che verlaufen, die sog. Geod&auml;ten. 
</p>
