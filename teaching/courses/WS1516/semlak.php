<b>Modus:</b> Die Lehrveranstaltung findet im "echten Seminarstil" statt, d.h. die Studierenden tragen in selbst gestalteten Referaten den Stoff vor. Jede/r Studierende (bei gro&szlig;er TeilnehmerInnenzahl sind auch Zweiergruppen m&ouml;glich) &uuml;bernimmt jeweils ein Thema und bereitet mit Unterst&uuml;tzung des Seminarleiters einen Vortrag vor: in einer Vorbesprechung werden inhaltliche/mathematische Aspekte gekl&auml;rt und auch Fragen der Pr&auml;sentationstechnik und "Vortragskultur" angesprochen. Nach dem Vortag erh&auml;lt jede/r Studierende ein detailliertes Feedback. Eine Abschlusspr&uuml;fung ist <b>nicht</b> vorgesehen. Kriterien f&uuml;r die Benotung sind neben dem eigenen Vortrag vor allem die Mitarbeit und Diksussionsbeitr&auml;ge zu Vortr&auml;gen der anderen TeilnehmerInnen. Eventuell (wiederum abh&auml;ngig von der TeilnehmerInnenzahl) werden einzelne Themen w&auml;hrend des Seminars gemeinsam gelesen und durchgearbeitet.
<br><br>

<b>Ziel:</b> Die Studierenden erlernen den eigenst&auml;ndigen Umgang mit einem mathematischen Text: Die Inhalte werden selbst&auml;ndig erarbeitet und strukturiert, um sie schlie&szlig;lich in fachgerechter Form zu pr&auml;sentieren und zu kommunizieren.      
<br><br>

<b>Notwendige Vorkenntnisse</b> sind die Inhalte der Vorlesungszyklen "Analysis" und "Lineare Algebra und Geometrie" aus dem ersten Studienabschnitt. Dabei können bei Bedarf fortgeschrittenere Inhalte insbesondere aus der mehrdimensionalen Analysis an Ort und Stelle wiederholt werden. Grundlegende Kenntnisse aus den gewöhnlichen Differentialgleichungen sind sicher nützlich aber nicht unbedingt erforderlich.
