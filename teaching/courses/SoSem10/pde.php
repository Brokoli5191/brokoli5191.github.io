<b>Allgemeines: </b> Partielle Differentialgleichungen (PDG) treten in einer Vielzahl von Wissenschaftsdiziplinen auf: 
Sie sind <i>die</i> Grundlage der Modellierung und Beschreibung von Naturph&auml;nomenen. Daher hat das Bem&uuml;hen
nach einem Verst&auml;ndnis von PDG und vor allem ihrer L&ouml;sungen schon lange eine zentrale Bedeutung in der Mathematik.
Wie etwa Algebra, Analysis und Topologie sind PDG daher ein Kernbereich der Mathematik und haben so unterschiedliche
Gebiete wie etwa Funktionalanalysis, Funktionentheorie und algebraische Topologie inspiriert.
<br><br>
Laut ihrer Definition ist eine PDG eine Gleichung f&uuml;r eine unbekannte Funktion <i>u</i>, die von zwei oder 
mehreren Variablen abh&auml;ngt und die gewisse partielle Ableitungen von <i>u</i> beinhaltet. 
Noch formaler ist eine PDG ein Ausdruck der Form
<center>
<i>F(D^k u(x), D^(k-1) u(x),...,Du(x),u(x),x)=0,</i><br><br>
</center> 
wobei <i>k</i> eine nat&uuml;rliche Zahl (die Ordnung der PDG) ist, <i>D^l u</i> die partiellen Ableitungen der Ordnung <i>l</i> bezeichnet,
<i>n>=2</i> ist und F:R^n^k x R^n^(k-1) x ... x R^n x R x U -> R</i> eine
gegebene Funktion. Die gesuchte Unbekannte ist hier eine Funktion <i>u:U -> R</i>, mit <i>U</i> einer offenen Teilmenge 
des <i>R^n</i>. Einfache Beispiele sind etwa die Laplacegleichung in zwei Dimensionen <i>u_xx+u_yy=0</i> oder die Wellengleichung
in einer Raumdimension <i>u_tt-u_xx=0,</i> aber etwa auch die Korteweg-de Vries Gleichung <i>u_t+uu_x+u_xxx=0</i>.
<br><br>
Ziel der Theorie der PDG ist es nun eine gegebene Gleichung zu "l&ouml;sen". In den meisten F&auml;llen kann aber keine
Rede davon sein, eine L&ouml;sung explizite anzugegeben. In dieser Situation ist es daher zun&auml;chst die Aufgabe der MathematikerInnen, 
die L&ouml;sbarkeit der Gleichung zu zeigen, d.h. zu beweisen, dass (in einem passenden Funktionenraum) L&ouml;sungen existieren. 
Besser noch ist es, wenn diese L&ouml;sungen unter der zus&auml;tzlichen Annhame von Rand- oder Anfangsbedingungen eindeutig sind und wenn sie 
(in einem geeigneteten Sinn) stetig von den "Daten" der Gleichung, also von <i>F</i> und den Rand- bzw. Anfangsbedingungen abh&auml;ngen --- 
letzteres ist nat&uuml;rlich vom Standpunkt der AnwenderInnen der entscheidende Punkt! Man spricht dann von einem wohlgestellten (engl: well-posed) 
Problem. Noch besser ist es nat&uuml;rlich, m&ouml;glichst viele Eigenschaften der L&ouml;sung zu bestimmen, wie etwa Langzeitverhalten, Asymptotik, etc.
<br><br>
Angesichts der Vielzahl der von PDG beschriebenen Ph&auml;nomene ist es nicht verwunderlich, dass es keine einheitliche Theorie aller
PDG gibt. Zwar ist eine derartige Theorie f&uuml;r lineare PDG, also solche Gleichungen, in die die Unbekannte und ihre partiellen Ableitungen "nur" linear eingehen, weitgehend entwickelt (L. H&ouml;rmander, The Analysis of Linear Partial Differential Operators, vol 1--4, 2nd ed., Springer, New York, 1990--94). F&uuml;r nichtlineare Gleichungen ist eine solche jedoch au&szlig;er Reichweite. Stattdessen werden verschiedene Klassen von PDG unter verschiedenen Aspekten studiert.
<br><br>
<b>Inhalt: </b> Diese Vorlesung bietet eine erste Einf&uuml;hrung in die Welt der PDG und folgt einem "elementaren" Aufbau, d.h.
sie kommt weitgehend ohne Verwendung der Funktionalanalysis und g&auml;nzlich ohne Verwendung der Distributionentheorie aus. Wir beschr&auml;nken uns 
nicht auf die klassischen (linearen!) Gleichungen, sondern schnuppern m&ouml;glichst fr&uuml;h ins "Nichtlineare" hinein. Wir folgen weitgehend
dem Buch L. Evans, Partial Differential Equations, AMS, vol. 19 of Graduate Studies in Mathematics, Providence, RI, 1998 und studieren im 
wesentlichen dessen "Part I". Etwas genauer sind die Inhalte:
<ul>
<li>Einleitung (Klassifizierung und Beispiele von PDG, L&ouml;sungsstrategien)</li>
<li>Vier klassische PDG (Transport-, Laplace-, W&auml;rmeleitungs- und Wellengleichung)</li>
<li>Nichtlineare PDG erster Ordnung (Methode der Charaktersitiken, Hamilton-Jacobi Gleichungen, Erhaltungss&auml;tze)</li>
<li>Darstellung von L&ouml;sungen (Trennung der Variablen, Transformationen, vor allem Fourier- und Laplacetransformation, ...)</li>
</ul>
