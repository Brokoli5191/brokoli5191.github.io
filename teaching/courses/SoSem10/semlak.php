<p align="justify">
<b>Allgemeines:</b><br>
Warum f&uuml;hren Transatlantikfl&uuml;ge &uuml;ber Island und Fl&uuml;ge nach Fernost &uuml;ber Sibirien?<br>
Ist Afrika wirklich nur so gro&szlig; wie Gr&ouml;nland?<br>
Was steckt hinter der Kontroverse um die Gall-Peters Karte (siehe Abbildung)?
<br><br>
<img width="70%" border="0" title="Gall-Peters Weltkarte" alt="Gall-Peters Weltkarte" src="../../images/peters-gall-map.jpg">
<br clear="left"><br>

Diese und andere Fragen werden wir beantworten indem wir einen Blick
auf die Mathematik werfen, die hinter der Erstellung von Weltkarten steht.  
<br><br>

Wir sind den allt&auml;glichen Umgang mit Landkarten gewohnt --- vom Stadtplan &uuml;ber Stra&szlig;enkarten zu Weltkarten.
Jede dieser Karten ist eine Darstellung, die notwendigerweise die Realit&auml;t vereinfacht um etwas
viel Gr&ouml;&szlig;eres und Komplexeres &uuml;bersichtlich abzubilden. Um ihren Zweck zu erf&uuml;llen
verzerrt jede Karte die Realit&auml;t und verwendet einen bestimmten Code --- in den allermeisten F&auml;llen
eine mathematische Transformation --- um das gew&uuml;nschte Objekt darzustellen. 
In diesem Seminar konzentrieren wir uns auf Weltkarten und die verschiedenen Eigenschaften, die diese haben 
k&ouml;nnen (winkeltreu, fl&auml;chentreu, etc.) und lernen die dahinterstehende mathematischen 
Konstruktionen kennen.
<br><br>

<b>Inhalt:</b> Als Hauptquelle des Seminars verwenden wir das Buch <a href="http://books.google.com/books?id=j1SFbvybvugC&pg=PA1&lpg=PA1&dq=portraits+of+the+earth&source=bl&ots=Ul4qqmL4MH&sig=ScmhXf1CbJwYhqpDEylKFCfnc-g&hl=en&ei=bhNaS6_yLoWunQOumJGaAg&sa=X&oi=book_result&ct=result&resnum=7&ved=0CBsQ6AEwBg#v=onepage&q=&f=false" target="_blank">Portraits of the Earth: A Mathematician Looks at Maps</a>
von <a href="http://www06.homepage.villanova.edu/timothy.feeman/" target="_blank">Timothy Feeman</a>. Die Kerninhalte sind:</p>
<ul>
<li>Grundlagen der Geod&auml;sie (Gr&ouml;&szlig;e und Form der Erde, L&auml;ngen- und Breitengrade)</li>
<li>Kartenprojektionen (azimuthale, Zylinder- und Kegelprojektionen)</li>
<li>Geometrie der Kugel (k&uuml;rzeste Wege, Winkel, Dreiecke und Fl&auml;chen, Kr&uuml;mmung)</li>
<li>Fl&auml;chentreue und winkeltreue Karten, Verzerrungen</li>
</ul> 
<p align="justify">
Je nach Interessenlage der TeilnehmerInnen sind kleine historische Ausfl&uuml;ge m&ouml;glich. Wir streifen
das Gebiet der Navigation und wir kommen auch in die N&auml;he nicht-Euklidischer Geometrien und der Geometrie von Fl&auml;chen.  
<br><br>

<b>Modus:</b> Die Lehrveranstaltung findet im "echten Seminarstil" statt, d.h. die Studierenden tragen in selbst gestalteten Referaten den Stoff vor.
Jeder Studierende (bei gro&szlig;er TeilnehmerInnenzahl sind auch Zweiergruppen m&ouml;glich) &uuml;bernimmt jeweils ein Kapitel (ca. 6-8 Seiten) des Buchs und
bereitet mit Unterst&uuml;tzung des Seminarleiters den Vortrag vor: in einer Vorbesprechung werden inhaltliche/mathematische Aspekte gekl&auml;rt und auch Fragen
der Pr&auml;sentationstechnik und "Vortragskultur" angesprochen. Nach dem Vortag erh&auml;lt jeder Studierende ein detailliertes Feedback. 
Eine Abschlusspr&uuml;fung ist <b>nicht</b> vorgesehen. Eventuell (wiederum abh&auml;ngig von der TeilnehmerInnenzahl) werden einzelne Kapitel w&auml;hrend des
Seminars gemeinsam gelesen und durchgearbeitet.
<br><br>

<b>Ziel:</b> Die Studierenden erlernen den eigenst&auml;ndigen Umgang mit einem mathematischen Text: Die Inhalte werden selbst&auml;ndig erarbeitet
und strukturiert, um sie schlie&szlig;lich in fachgerechter Form zu pr&auml;sentieren und zu kommunizieren.      
<br><br>

<b>Vorkenntnisse:</b> Es sind keine Vorkenntnisse in Kartographie erforderlich. Die n&ouml;tigen mathematischen Vorkenntnisse sind
etwas Analysis (Winkelfunktionen, Differential- und Integralrechnung), sowie ein wenig Elementargeometrie und an manchen Stellen 
etwas Lineare Algebra. Der Haupttext ist in zwar in englischer Sprache verfa&szlig;t aber mit Englisch auf Maturaniveau lesbar. (Technische Texte
sind im allgemeinen wesentlich leichter zu lesen als literarische.) 
<br><br>

<B>Zielpublikum:</B> Lehramtsstudierende der Mathematik mit Freude am Thema und Spa&szlig; am eigenst&auml;ndigen Arbeiten. Lehramtsstudierende mit
Erst- oder Zweitfach Geographie sind nat&uuml;rlich besonders willkommen. 
