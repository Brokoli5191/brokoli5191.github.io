      <p align="justify">
        <b>Inhalt:</b> In diesem Seminar werden wir im Rahmen der <em>elementaren Differentialgeometrie</em> einigen der oben skizzierten Entwicklungen
        nachspüren und uns mit einigen Grundbegriffen der modernen Differentialgeometrie, insbesondere dem Begriff der <em>Krümmung</em>
        beschäftigen. Hier bedeutet "elementar" nicht etwa, dass es sich um besonders einfache Sachverhalte handelt, sondern, dass diese ohne
        Verwendung des umfassenden formalen Begriffsappartats der Differentialgeometrie (Mannigfaltigkeiten, Bündel, Schnitte, etc.) beschrieben werden.
        So werden wir uns nach einem kurzen motivierenden Ausflug in die <em>sphärische Geometrie</em> und eventuell einer kurzen Darstellung
        der <em>euklidischen Geometrie</em> der klassichen Theorie der <em>Kurven</em> und <em>Flächen</em> zuwenden. Unsere Hauptquelle ist das
        Buch <a href="http://books.google.com/books?id=rvLvfEMQZL0C&printsec=frontcover&dq=b%C3%A4r+elementare+differentialgeometrie&hl=en&ei=UnUbTrjEGdGe-Qbuson5Dw&sa=X&oi=book_result&ct=result&resnum=1&ved=0CCsQ6AEwAA#v=onepage&q&f=false" target="_blank"><em>Elementare Differentialgeometrie</em></a> 
        von <a href="http://geometrie.math.uni-potsdam.de/index.php/de/mitarbeiter/prof-dr-christian-baer" target="_blank">Christian Bär</a>. 
      </p>

