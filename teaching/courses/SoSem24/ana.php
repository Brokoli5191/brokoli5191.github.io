                <li class="list-dot">Information zu Bepunktung des MC-Teils<br>
                Bei jeder der MC-Fragen gibt es 4 Antwortmöglichkeiten, wobei jeweils eine, zwei oder drei davon korrekt sind (niemals keine=0 bzw. alle=4 der Möglichkeiten). Es wird allerdings nicht angegeben, ob eine, zwei oder drei Antwortmöglichkeiten korrekt sind.<br><br>
                
                Die Bepunktung erfolgt nun so: Für eine gekreuzte richtige Antwort werden 1/(# der richtigen Möglichkeiten) Punkte vergeben (dh. 1 Pkt. bei einer korrekten Antwortmöglichkeit, 1/2 Pkt. bei 2 und 1/3 Pkt. bei 3 korrekten Möglichkeiten). Für eine gekreuzte falsche Antwort werden ebenso 1/(# der richtigen Möglichkeiten) Punkte abgezogen (dh. 1 Pkt. bei einer korrekten, 1/2 Pkt. bei 2 und 1/3 Pkt. bei 3 korrekten Möglichkeiten). Wird eine falsche Antwort oder eine richtig Antwort nicht gekreuzt, gibt es keine Punkte. Insgesamt werden pro Frage aber keine negativen Punkte vergeben, sodass bei jeder Frage zwischen 0 und 1 Punkte vergeben werden.</li>
              </ul>
              
