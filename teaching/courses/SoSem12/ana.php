<p align="justify"><b>Inhalt:</b><a name="inhalt"></a>
Aufbauend auf der ,,Einf&uuml;hrung in das mathematische Arbeiten'' (EMA), insbesondere auf den
Eigenschaften der reellen (und komplexen) Zahlen und dem Funktionsbegriff entwickeln wir die
Grundbegriffe der Analysis. <br>

Nach einer kurzen Zusammenstellung der f&uuml;r uns relevanten Resultate der EMA
(insbesondere: R als ordnungsvollst&auml;ndiger geordneter K&ouml;rper) wenden wir uns <em>reellen Folgen</em>
und dem <em>Grenzwertbegriff (Limes), dem</em> zentralen Begriff der gesamten Analysis zu. Eine Folge
konvergiert gegen ein Zahl, ihren Grenzwert, falls sie diesem schlie&szlig;lich beliebig nahe kommt. 
Wie ein roter Faden zieht sich die (Ordnungs-)Vollst&auml;ndigkeit der reellen Zahlen durch dieses 
Kapitel zu dessen Abschluss wir unendliche <em>Reihen</em> studieren.<br>

Als n&auml;chstes wenden wir uns <em>stetigen Funktionen</em> einer reellen Variable
zu. Diese ,,sch&ouml;nen'' Funktionen haben die Eigenschaft, dass die Bildwerte ,,nahe beieinander'' 
liegender Punkte ebenfalls ,,nahe beieinander'' liegen bzw. dass sie konvergente Folgen im Definitionsbereich
in konvergente Folgen im Wertebereich &uuml;bersetzen. Viele bekannte Funktionen wie die Exponential- und 
die Logarithmusfunktion sowie die Winkelfunktionen fallen in diese Klasse und werden an dieser Stelle
genau untersucht.
<!--Schlie&szlig;lich kommen wir zur <em>Differentiation</em>: Wir formalisieren die Idee, dass eine 
in einem Punkt differenzierbare Funktion ebendort durch ihre Tangente ersetzt werden kann. Es handelt
sich dabei um eine fundamentale Eigenschaft mit weitreichenden Konsequenzen, die wir ausf&uuml;hrlich
untersuchen.
-->
</p>

<p align="justify"><b>Literatur:</b> Inhaltlich werde ich mich eng an das 
<a target=_blank" 
href="http://www.mat.univie.ac.at/~gue/lehre/08einan/einfanalysis.pdf" >gleichnamige Skriptum</a> von
