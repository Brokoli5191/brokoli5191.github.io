<h3><a name="RAimukAieVfLAK"></a>Reelle Analysis in mehreren und komplexe Analysis 
in einer Variable für LAK</h3>

<b>Lehrveranstaltungsnummer:</b> 250057<br>
<b>Lehrveranstaltungstyp:</b> VO<br>
<b>Semesterwochenstunden/ECTS:</b> 5/10<br>
<b>Zeit:</b> Mo. und Do. 10:05-11:55 (inkl. 10' Pause), Mi. 10:05-10:55<br>
<b>Ort:</b> H&ouml;rsaal 3 (UZA 2)<br>
<br><br>


<p align="justify"><b>Inhalt:</b><a name="inhalt"></a>
Aufbauend auf der <a href="#AieVfLAK">,,Analysis in einer Variable f&uuml;r LAK 
(AieVfLAK)''</a> 
vom Wintersemester 2012/13 befassen wir uns vor allem mit der  mehrdimensionalen Analysis,  
also der Differential- und Integralrechnung von Funktionen f:R^n->R^m. Weiterhin
ist der Leitfaden der Vorlesung also das Studium des &Auml;nderungsverhaltens
von Funktionen (vgl. <a href="#allg">Was will und was soll die Analysis</a>),
allerdings jetzt von Funktionen mit mehrdimensionalem Definitions- und Zielbereich.<br>

Zun&auml;chst befassen wir uns aber mit <i>Funktionenfolgen</i> die auf R oder C definiert 
sind. Wir studieren die Begriffe der punktweisen und der gleichm&auml;&szlig;igen
Konvergenz und fragen nach der Vertauschbarkeit von Limes und Differentiation bzw. 
Integration. Wir behandeln Potenzreihen und den Satz von Taylor und geben einen kleinen
Einstieg in die Theorie der Fourier-Reihen.<br>

Wir beginnen unsere Darstellung der <i>mehrdimensionalen Differentialrechnung</i>
mit einer kurzen Abhandlung zur Topologie des R^n und studieren Konvergenz und
Kompaktheit. Danach befassen wir uns mit Funktionen f: R^n->R^m, beginnend mit
ihrer Stetigkeit. Das gro&szlig;e Thema ist allerdings das der Differenzierbarkeit
derartiger Funktionen und Anwendungen der Differentialrechnung (Extremwerte, Umkehrsatz,
Satz &uuml;ber implizite Funktionen). Schlie&szlig;lich streifen wir die Theorie der Kurven
im $R^n$.<br>

Danach wenden wir uns der <i>mehrdimensionalen Integralrechnung</i> und ihren
Anwendungen (Volums- und Fl&auml;chenberechnung) zu. Wir streifen kurz die Theorie
von Fl&auml;chen im R^n und behandeln Oberfl&auml;chenintegrale und die klassischen 
Integralsätze (S&auml;tze von Stokes und Gau&szlig;)<br>

Wir schlie&szlig;en die Vorlesung mit einem Abri&szlig und Ausblick auf die
<i>komplexe Analysis</i>, also die Analysis von Funktionen, die auf C definiert sind.
</p>

