</a><h2>Allgemeines: Was will und was soll die Analysis?</h2>

<p align="justify">
<b>Inhaltlich</b> besch&auml;ftigt sich die Analysis vor allem mit <em>Funktionen</em>, und zwar zun&auml;chst mit
solchen von R nach R. Ganz allgemein dienen Funktionen dazu, den Zusammenhang zwischen verschiedenen Gr&ouml;&szlig;en
zu beschreiben. Hier meint "beschreiben" nicht "erkl&auml;ren"---das Wort Zusammenhang ist also nicht kausal
zu verstehen sondern es geht darum, welche Werte einer Gr&ouml;&szlig;e zusammen mit welchen Werten einer anderen 
Gr&ouml;&szlig;e auftreten. Allt&auml;gliche Beispiele sind etwa Verzinsung (H&ouml;he eines Guthabens oder auch 
eines Schuldenstands zum einem bestimmten Zeitpunkt), Bremsweg (L&auml;ge des Bremswegs in Abh&auml;ngigkeit von  der 
Geschwindigkeit), Radiokativit&auml;t (Anzahl der strahlenden Isotope zu einem bestimmten Zeitpunkt) etc.
</p>

<table align="right" width="50%">
 <tr><td align="center">Bremsweg in Abh&auml;ngigkeit von der Geschwindigkeit</th></tr>
 <tr><td><img src="http://www.mat.univie.ac.at/~stein/teaching/SoSem12/bremsweg.gif" alt="Bremsweg" width="90%" name="Bremsweg"></td></tr>
</table>
<p align="justify">
Ein wichtiger Kunstgriff der Mathematik ist die <em>Abstraktion</em>: Es wird von der konkreten Situation 
bewu&szlig;t abgesehen und statt mit Bremsweg, Zeit, Energieverbauch, etc. besch&auml;ftigen wir uns mit 
,,anonymen'', universell verwendbaren Gr&ouml;&szlig;en, den Variablen. Alles was wir &uuml;ber 
Funktionen herausfinden, ist also universell (in jedem Beispiel) g&uuml;ltig, wobei nat&uuml;rlich eine 
exakte mathematische Erkl&auml;rung (sprich Definition), was eine Funktion sein soll, zugrundegelegt werden 
muss---das ist Stoff der ,,Einf&uuml;hrung in das Mathematische Arbeiten'' (EMA).
<br><br>
Darauf und auf der Definition der reellen Zahlen (siehe ebenfalls EMA) aufbauend ist es der rote Faden
der Analysis, das &Auml;derungsverhalten von Funktionen zu verstehen, zu beschreiben und zu beherrschen.
<!--(, 
was etwas pr&auml;ziser formuliert auf folgende Fragestellungen hinausl&auml;ft 
nach [Heuser 2003]):
-->
Das Studium von funktionellem &Auml;nderungsverhalten ist, wie schon oben angedeutet, keineswegs ein rein
,,akademisches Vergn&uuml;gen'', sondern ist eng verbunden mit dem Bestreben der Menschen die uns umgebende Welt
zu verstehen und zu gestalten. Das zeigt insbesondere die Geschichte der Analysis, deren Entstehen und 
Meilensteine Hand in Hand gehen mit der Entwicklung der neuzeitlichen Physik durch 
<!--Geistesgr&ouml;&szlig;en wie--> 
Newton, Euler, Lagrange und Laplace, um nur die gro&szlig;en Denker des Anfangs zu nennen. 
Insofern ist die Analysis fundamentaler Bestandteil der naturwissenschaftlich-technischen Revolution, die in 
den letzten 4 Jahrhunderten unsere Welt so tiefgreifend und beispiellos ver&auml;ndert hat.
</p>

<p align="justify">
Etwas <b>technischer</b> formuliert sind die zentralen Fragestellungen beim Studium des &Auml;nderungsverhalten
von Funktionen:
<ul>
 <li>Welche Begriffe eignen sich am besten dazu das Verhalten von Funktionen ,,im Kleinen'' zu erfassen.</li>
 <li>Wie kann man aus dieser Kenntnis einer Funktion ,,im kleinen'' Aussagen &uuml;ber die Funktion im Ganzen
  gewinnen.</li>
</ul>

Jede tiefgreifende Untersuchung dieser Fragen f&uuml;hrt auf den Begriff des <em>Grenzwerts (Limes)</em>
zun&auml;chst von Folgen und dann &uuml;ber den Begriff der <em>Stetigkeit</em> von Funktionen zur 
<em>Differential- und Integralrechnung</em>.
</p>

<p align="justify">
Die Analysis ist jedoch weit mehr als ein Lehrsystem indem diese abstrakten Begriffe zu abstrakten 
Resultaten verwoben werden. Sie bringt in schier unglaublicher Methodenvielfalt eine F&uuml;lle
konkreter mathematischer Resultate hervor. Dabei steht oft der Gedanke der <em>Approximation</em>
im Zentrum, es ergeben sich eine Unzahl ,,sch&ouml;ner'' Formeln und Identit&auml;ten und immer wieder
k&ouml;nnen &uuml;berraschende Beziehungen zwischen Begriffen hergestellt werden, die zun&auml;chst nichts 
miteinander zu tun haben.
</p>

<p align="justify">
<b>Methode:</b> Sie haben das Gl&uuml;ck diese Begriffe in einer vergleichsweise verst&auml;ndlichen Form 
kennen lernen zu k&ouml;nnen. Das war nicht immer so, denn bis weit ins 19. Jahrhundert waren die 
MathematikerInnen auf eine mehr oder weniger gut funktionierende Intuition beim Umgang mit ,,unendlich 
kleinen Gr&ouml;&szlig;en'' angewiesen. 
Heutzutage versteht es sich aber von selbst, dass jede Darstellung der Analysis der <em>axiomatischen
Methode</em> zu folgen hat. Die ganze Theorie und alle ihre Aussagen m&uuml;ssen in streng logischem Aufbau aus den
Grundeigenschaften der reellen Zahlen aufgebaut werden. Jede mathematische Disziplin verdankt ihre
Sicherheit aber oft auch ihre Sch&ouml;nheit dieser Methode. <br><br>

Andererseits ist es eben dieser abstrakte Zugang, der die Anf&auml;ngerInnen vor die gro&szlig;e
Herausforderung stellt, den deduktiven Aufbau mit ihrem Vorwissen, ihrer Intuition, Phantasie und 
Kreativit&auml;t in Einklang zu bringen. Dazu geh&ouml;rt auch das Erlernen und selbstverst&auml;ndliche
Verwenden der Fachsprache und insofern wird hier auch der methodische roter Faden aus der EMA &uuml;bernommen.   
</p>

