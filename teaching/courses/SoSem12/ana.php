<p align="justify"><b>Inhalt:</b><a name="inhalt"></a>
Aufbauend auf der <a href="#ea">,,Einf&uuml;hrung in die Analysis'' (EidA)</a> 
vom Sommersemester 2012 wird die Differential- und Integralrechnung von 
Funktionen f:R->R entwickelt. Dabei nimmt unser Studium des &Auml;nderungsverhaltens
von Funktionen (vgl. <a href="#allg">Was will und was soll die Analysis</a>)
eine alles entscheidende Wendung.<br>

Zun&auml;chst befassen wir uns mit dem Begriff der
<i>Differenzierbarkeit:</i> Eine Funktion heißt differenzierbar in einem Punkt
ihres Definitionsbereichs, falls sie in der Nähe dieses Punkts ,,gut'' durch
eine Gerade (ihre Tangente) approximiert werden kann. Nach einem 
gr&uuml;ndlichen Studiums dieses Begriffs untersuchen wir die Eigenschaften
differenzierbarer Funktionen und lernen ein erstes Hauptresultat der Vorlesung
kennen: den <i>Mittelwertsatz der Differentialrechnung</i>.<br>

Danach wenden wir uns der Integralrechnung zu und studieren 
ausf&uuml;hrlich  das <i>Riemann Integral</i>. 
Eine Funktion hei&szlig;t Riemann integrierbar, falls
sie zwischen stückweise konstanten Funktionen (sog. Treppenfunktionen) 
,,gut eingezwickt'' werden kann. Die Verbindung zwischen Integral und 
Differential stellen wir mit einem weiteren Hauptresultat der Vorlesung, 
dem <i>Hauptsatz der Differential- und Integralrechnung</i> her. Danach lernen wir
den <i>Satz von Taylor</i> kennen, der es erlaubt den Gesamtverlauf 
(,,sch&ouml;ner'') Funktionen allein aus der Kenntnis ihrer Ableitungen in 
<i>einem</i> Punkt zu rekonstruieren.<br>

Dieser Satz f&uuml;hrt uns weiter zum Studium von Potenzreihen und allgemeiner
von <i>Funktionenfolgen und -reihen</i>---also Folgen bzw. Reihen wo die Glieder nicht
reelle Zahlen sind (wie in der <a href=ea">EidA</a>) sondern Funktionen. 
Hier lernen wir die beiden
zentralen Konvergenzbegriffe, die punktweise und die gleichm&auml;&szlig;e
Konvergenz kennen und unterscheiden. Schließlich unternehmen wir noch einen
kleinen Ausflug in das Reich der <i>Fourierreihen</i>, die es ermöglichen 
periodische Funktionen in Grund und Oberschwingungen zu zerlegen und so eine
zentrale Rolle in vielen Anwendnungsbereichen spielen z.B. in der Elektronik 
und Mobilkommunikation.
</p>


