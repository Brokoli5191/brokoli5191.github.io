<b>Allgemeines:</b> 
,,Wissenschaft genie&szlig;t hohes Ansehen. Offensichtlich ist es eine weit verbreitete Annahme, dass es mit der Wissenschaft und ihren Methoden 
etwas besonderes auf sich habe. Die Bezeichnung einer Aussage, eines Gedankengangs oder eines Forschungsvorhabens als ,,wissenschaftlich''
wird in einer Art und Weise vorgenommen, die einen Verdienst oder eine spezielle Art der Zuverl&auml;ssigkeit impliziert. Aber was ist das
Besondere an Wissenschaft? Was ist diese ,,wissenschaftliche Methode'', die angeblich zu so besonders verdienstvollen oder zuverl&auml;ssigen
Ergebnissen f&uuml;hrt?'' [nach A. Chalmers, 2007].<br>
In dieser Lehrveranstaltung wollen wir derartige Fragen beleuchten und verschiedenen Antworten nachgehen.
<br><br>


<b>Inhalt</b> dieses Konversatoriums ist eine Einf&uuml;hrung die Wissenschaftstheorie vor allem der Naturwissenschaften.
Wir werden die wesentlichen wissenschaftstheoretischen Positionen und Schulen des 20. Jahrhunderts kennenlernen.  &Uuml;ber die Diskussion des logischen 
Positivismus des Wiener Kreises, des Falsifikationismus Karl Poppers, Thomas Kuhns Ansatz vom Paradigmenwechsel und Imre Lakatos' 
Begriffs der Forschungsprogramme werden wir sehen, dass es nicht (einfach) begr&uuml;ndbar ist, dass Wissenschaft auf universellen, 
ahistorischen Ma&szlig;st&auml;ben und Methoden beruht. Paul Feyerabends anarchistische Wissenschaftstheorie hingegen f&uuml;hrt auf einen 
Relativismus, der eine Abgrenzung von Wissenschaft gegen&uuml;ber Pseudowissenschaften wie etwa der Astrologie verunm&ouml;glicht (und auch nicht
beabsichtigt, ,,anything goes''). Damit n&auml;hern wir
uns den aktuellen Kontroversen, die zwischen den Polen einer radikal relativistischen Wissenschaftstheorie und oft auch -soziologie ("Wissenschaft als soziales Konstrukt") und einem positivistischen Standpunkt
zahlreiche Facetten aufweisen ("Science Wars"). <br>

Als Hauptquelle werden wir ,,Wege der Wissenschaft'' von Alan F. Chalmers [Springer, 6. verbesserte Auflage 2007, Berlin] heranziehen,
der in der oben beschriebenen Kontroverse einen Mittelweg zu gehen versucht. Ausgehend von diesem Text und seiner Fortsetzung [Alan F. Chalmers, 
,,Grenzen der Wissenschaft'', Springer, Berlin 1999] wollen wir, je nach Interessenslage der TeilnehmerInnen, verschiedene Themen, Ergänzungen und Aspekte anrei&szlig;en, etwa Lakatos'
Wissenschaftstheorie der Mathematik, Bedeutungstheorie, die
,,Sokal-Affäre'', feministische Ansätze zur Wissenschaftstheorie,
Konstruktiver Realismus, evolutionäre Erkenntnistheorie, Bezüge zur
analytischen Sprachphilosophie, ect.
 

