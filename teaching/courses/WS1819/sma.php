<b>Allgemeines:</b> Die Analysis ist eines der zentralen Teilgebiete der Mathematik und in seiner Bedeutung kaum zu &uuml;bersch&auml;tzen.
Ebenso ist die Schulanalysis eine der tragenden S&auml;len der Schulmathematik. Ihr Kern besteht aus der Differential- und Integralrechnung 
für reelle Funktionen in einer Variablen, ihr zentraler Begriff ist der des Grenzwerts.<br><br>      

Als <b>Ziel</b> der Vorlesung (und der zugeh&ouml;rigen &Uuml;bungen) formuliert das Curriculum: <i>
Die Studierenden erkennen die Relevanz der fachmathematischen Konzepte für den
Schulunterricht und k&ouml;nnen diese dort angemessen verwenden. Sie kennen
verschiedene M&ouml;glichkeiten für Zug&auml;nge zu grundlegenden Themen des Analysis-
Schulunterrichts (und ihrer Anwendungen) und k&ouml;nnen diese bewerten. Die Studierenden
k&ouml;nnen in diesem Gebiet fachdidaktische Konzepte anwenden und Computer in
angemessener Weise einsetzen, sie kennen typische Fehlvorstellungen und passende
Interventionsm&ouml;glichkeiten.</i>
<br><br>

Um dieses Ziel zu erreichen, werden wir in der Vorlesung die Schulanalysis von einem h&ouml;heren Standpunkt aus betrachten. 
Das bedeutet, dass der Hauptstrang der Vorlesung die zentralen Inhalte der Schulanalysis behandelt, aus verschiedenen Perspektiven beleuchtet 
und ein umfassendes Verst&auml;ndnis der analytischen Kernbegriffe und ihrer Zusammenh&auml;nge herstellt. Dabei werden wir einerseits
an die Inhalte und (mathematischen) Methoden der Fachvorlesung ,,Analysis in einer Variable für das Lehramt'' ankn&uuml;pfen und andererseits 
ausf&uuml;hrlich Bez&uuml;ge zur Unterrichtspraxis herstellen. Wir stellen einen fachdidaktischen Begriffsrahmen zur Verf&uuml;gung, der es erlaubt, 
das mathematisches Fachwissen zu reflektieren und mathematikdidaktisches Professionswissen aufzubauen. Damit sollen die H&ouml;rer/innen 
bef&auml;higt, werden einen qualit&auml;tsvollen Analysis-Unterricht zu gestalten, der fachlich und fachdidaktisch fundiert ist und sich durch 
eine reflektierte Unterrichtspraxis ausgezeichnet.
<br><br>

<b>Hochschuldidaktischer Hintergrund:</b> 
Es gibt eine umfangreiche Literatur zum sog. Lehrerprofessionswissen. Durch große empirische Studien
ist belegt, dass sich das mathematische Lehrerwissen valide in die beiden Teilbereiche mathematical
content knowledge (MCK) und paedagogical context knowledge (PCK) unterteilen lässt. Letzteres könnte
man auch kurz „fachdidaktisches Handlungswissen“ nennen und ist Hauptpr&auml;diktor für den Lernerfolg
von Sch&uuml;lern/innen. MCK der Lehrkraft hat zwar auch einen positiven Einfluss auf die Unterrichtsqualit&auml;t,
reicht aber alleine nicht aus. Allerdings beruht PCK immer auf einer soliden Basis von MCK.
<br><br>

Dar&uuml;ber hinaus sind typische unterrichtliche Handlungsanforderungen an Lehrkr&auml;fte ebenfalls gut empirisch erforscht. 
Es ergeben sich z.B. folgende mathematischen Kernaufgaben, die Lehrer/innen in ihrer täglichen Praxis zu bewältigen haben:
<ul>
<li> Anforderungen an Sch&uuml;ler/innen (aus Schulb&uuml;chern, Tafelbildern oder
Tests) selbst bew&auml;ltigen und auf verschiedenen Niveaus bearbeiten können</li>
<li> Lernziele setzen und aussch&auml;rfen</li>
<li> Zug&auml;nge (in Schulb&uuml;chern, Tafelbildern o.&auml;.) analysieren und bewerten</li>
<li> Aufgaben und Lernanl&auml;sse auswählen, ver&auml;ndern oder konstruieren</li>
<li> Tests entwickeln und re-skalieren</li>
<li> geeignete Darstellungen und Exaktheitsstufen ausw&auml;hlen und nutzen sowie zwischen
ihnen vermitteln</li>
<li> &Auml;ußerungen von Lernenden analysieren, bewerten und darauf lernf&ouml;rderlich reagieren</li>
<li> Fehler von Lernenden analysieren und darauf lernf&ouml;rderlich reagieren</li>
<li> fachlich substantielle, produktive Diskussionen moderieren</li>
<li> zwischen verschiedenen Sprachebenen (Alltagssprache,
 Fachsprache,Symbolsprache) flexibel hin-und herwechseln</li>
<li> Lernst&auml;nde, Lernprozesse und Lernerfolge erfassen</li>
<li> Begriffe und Konzepte erklären und Bez&uuml;ge herstellen</li>
</ul>
Ebenso wird in der Literatur darauf hingewiesen, dass fachliche Inhalte im Bewusstsein der Lehrkr&auml;fte geeignet 
repr&auml;sentiert sein m&uuml;ssen, damit diese f&auml;hig sind, Aufgaben des obigen Anforderungskatalogs effektiv zu bew&auml;ltigen.
<br><br>

Die Orientierung an obigen Punkten liefert uns ein leitendes Prinzip, um die
Inhalte und ihre konkrete Ausgestaltung, die Methoden und den Geist der Vorlesung praxisnahe zu 
gestalten. In der Vorlesung aber vor allem in den &Uuml;bungen  werden wir diese Kernaufgaben 
trainieren und ein Bewusstsein für die mathematischen T&auml;tigkeiten schaffen, die die Unterrichtspraxis bestimmen.
<br><br> 
 

<b>Inhalt:</b> 
<ul>
<li>Was will und was soll die (Schul-)analysis</li>
<li>Fachdidaktischer Begriffsrahmen (Grunderfahrungen, Grundvorstellungen und Aspekte)</li>
<li>Funktionsbegriff</li>
<li>Folgen, Grenzwert und Vollst&auml;ndigkeit der reellen Zahlen</li>
<li>Differentialrechnung</li>
<li>Integralrechung</li>
<li>Spezielle Funktionen</li>
</ul>
