<P ALIGN="justify">
<B>Inhalt</B> dieser Lehrveranstaltung, bei der Vorlesung
und &Uuml;bungen eine Einheit bilden, ist die Installation eines
Linux-Computers, quasi vom „Auspacken aus der Schachtel bis zur 
Anbindung an ein Netzwerk“.
</P>
<P ALIGN="justify">
Die <EM>Vorlesung</EM>, die an zwei Freitag-Nachmittagen (voraussichtlich Ende 
November) geblockt stattfinden wird, soll eine sehr komprimierte Einf&uuml;hrung
in den Betrieb eines Linux(unix)-Systems geben. Im ersten Block werden die 
Grundz&uuml;ge des Systems dargelegt und eine Minimaleinf&uuml;hrung in seine Benutzung
gegeben. Der zweite Block behandelt die grunds&auml;tzlich bei der Installation
eines Linux-Rechners durchzuf&uuml;hrenden Schritte.
</P>
<P ALIGN="justify">
Die <EM>&Uuml;bungen</EM> finden ebenfalls geblockt (voraussichtlich im Dezember 
und J&auml;nner) statt. Kleingruppen von 3&ndash;5 Studenten werden – angeleitet von 
Tutoren – selbst&auml;ndig ein (RedHat)-Linux-System auf einem PC installieren.
</P>
<P ALIGN="justify">
<B>Voraussetzungen</B> sind gute PC-Grundlagen. Der Besuch der 
Lehrveranstaltung <EM>
<A HREF="http://diana.mat.univie.ac.at/~susanne/advanced/aushang.html">
Advanced Internet</A></EM> von 
<EM><A HREF="http://diana.mat.univie.ac.at/~susanne">Susanne Kandl</A></EM>
(die geblockt im Oktober und November stattfindet) wird empfohlen.
</P>
<P ALIGN="justify">
<B>Anrechenbarkeit:</B> Wahlfach Informatik (Studium Mathematik),
LA-Studium Informatik (offizielle Einrichtung voraussichtlich ab 
WS 2000/2001)
</P>
