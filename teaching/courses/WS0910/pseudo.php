              <h3>Contents:</h3>
              <p>At least for the first three quarters of the course we
              will rather closely follow the book <i>Elementary Introduction to the Theory
              of Pseudodifferential Operators</i> by Xavier Saint Raymond (Studies in
              Mathematics, CRC-Press, Boca Raton, 1991). The main chapters will be:</p>
              <ul>
                <li><i>0. Motivation and Introduction</i></li>
                <li><i>1. Prerequisites:</i> A short collection of the basic
                facts on Fourier transform and Sobolev spaces.</li>
                <li><i>2. Pseudodifferential Symbols:</i> Symbols and their asymptotic expansions,
                oscillatory integrals, operations on symbols.</li>
                <li><i>3. Pseudodifferential operators:</i> definition and action in
                S, S', and H^s, change of variables.</li>
                <li><i>4. Applications:</i> local solvability of linear PDEs, 
                microlocal analysis, propagation of singularities, ...</li>
              </ul>
              
