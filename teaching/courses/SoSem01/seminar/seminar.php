<P>
Dieses Seminar bietet eine Einführung in aktuelle Entwicklungen auf 
dem Gebiet der Algebren Verallgemeinerter Funktionen (Colombeau Algebren) und
hier insbesondere in ihre geometrischen Aspekte, die in den letzten
Jahren vor allem von der Forschungsgruppe 
<A HREF="http://www.mat.univie.ac.at/~diana"> DIANA
(DIfferential Algebras and Nonlinear Analysis), 
</A>
in Wien und Innsbruck untersucht wurden. 
<P>
Die StudentInnen sollen sich dem Stoffgebiet anhand von selbstvorbereiteten 
Seminarvortägen nähern. Neben dem fachlichen Inhalt des Seminars ist 
uns vor allem die Vermittlung von Präsentationstechnik und fachlicher 
Diskussionskultur ein großes Anliegen, das in diesem Seminar nicht zu kurz 
kommen wird.
<br><br>
<B>Voraussetzungen:</B>
Das Seminar wendet sich an StudentInnen im 2. 
Studienabschnitt, die knapp vor Beginn ihrer Diplomarbeit stehen.
Solide Analysis-Kenntnisse werden vorausgesetzt, Kenntnisse in 
Differentialgeometrie und Funktionalanalysis sind wünschenswert.
